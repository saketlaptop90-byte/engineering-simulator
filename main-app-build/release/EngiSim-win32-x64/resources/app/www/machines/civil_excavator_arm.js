import { plastic, aluminum, glass, copper, steel, darkSteel, rubber, chrome, tinted } from '../utils/materials.js';

export function createMachine(THREE) {
    const group = new THREE.Group();
    const parts = [];

    const glowingHydraulic = new THREE.MeshPhysicalMaterial({
        color: 0x00ffff, emissive: 0x0088cc, emissiveIntensity: 1.5,
        roughness: 0.2
    });

    const baseGeo = new THREE.BoxGeometry(4, 2, 4);
    const baseMesh = new THREE.Mesh(baseGeo, darkSteel);
    group.add(baseMesh);

    // Boom (The large first arm attached to the cab)
    const boomGeo = new THREE.BoxGeometry(1, 8, 1.5);
    const boomMesh = new THREE.Mesh(boomGeo, steel);
    boomMesh.position.set(0, 4, 0); 
    
    const boomPivot = new THREE.Group();
    boomPivot.position.set(0, 1, 0); // Attach to base
    boomPivot.rotation.z = Math.PI / 6; // Angle forward
    boomPivot.add(boomMesh);
    group.add(boomPivot);
    
    parts.push({
        name: "Main Boom",
        description: "The massive heavy-duty steel arm attached to the rotating cab.",
        material: "Thick Steel Plate",
        function: "Provides the primary lifting height and downward digging force.",
        assemblyOrder: 1,
        connections: ["Cab Pivot", "Stick Arm", "Boom Cylinders"],
        failureEffect: "Structural weld crack.",
        cascadeFailures: ["Arm completely detaches under load"],
        originalPosition: {x:0, y:0, z:0},
        explodedPosition: {x:-5, y:5, z:0}
    });

    // Stick/Dipper (The second arm)
    const stickGeo = new THREE.BoxGeometry(0.8, 6, 1.2);
    const stickMesh = new THREE.Mesh(stickGeo, steel);
    stickMesh.position.set(0, -3, 0);
    
    const stickPivot = new THREE.Group();
    stickPivot.position.set(0, 8, 0); // Attach to end of boom
    stickPivot.rotation.z = -Math.PI / 3; // Angle back down
    stickPivot.add(stickMesh);
    boomPivot.add(stickPivot);
    
    parts.push({
        name: "Dipper Stick (Arm)",
        description: "The secondary arm attached to the boom.",
        material: "Thick Steel Plate",
        function: "Reaches out and provides the primary sweeping/pulling motion for digging.",
        assemblyOrder: 2,
        connections: ["Boom", "Bucket", "Stick Cylinder"],
        failureEffect: "Metal fatigue.",
        cascadeFailures: ["Arm snaps while prying bedrock"],
        originalPosition: {x:0, y:0, z:0},
        explodedPosition: {x:5, y:5, z:0}
    });

    // Bucket
    const bucketGeo = new THREE.CylinderGeometry(1.5, 1.5, 1.2, 3, 1, false, 0, Math.PI); // Half cylinder
    const bucketMesh = new THREE.Mesh(bucketGeo, darkSteel);
    bucketMesh.rotation.x = Math.PI / 2;
    bucketMesh.position.set(0, -1, 0);

    const bucketPivot = new THREE.Group();
    bucketPivot.position.set(0, -3, 0); // Attach to end of stick
    bucketPivot.rotation.z = Math.PI / 4; // Curl in
    bucketPivot.add(bucketMesh);
    stickPivot.add(bucketPivot);

    parts.push({
        name: "Digging Bucket & Teeth",
        description: "Heavy steel scoop with hardened replaceable teeth.",
        material: "Hardox Steel / Carbide",
        function: "Penetrates the earth, shears roots/rocks, and holds the excavated material.",
        assemblyOrder: 3,
        connections: ["Stick", "Bucket Cylinder"],
        failureEffect: "Lost tooth.",
        cascadeFailures: ["Reduced digging efficiency", "Jam in rock crusher"],
        originalPosition: {x:0, y:0, z:0},
        explodedPosition: {x:0, y:-5, z:0}
    });

    // Hydraulic Cylinders (Visual approximation)
    const cylGeo = new THREE.CylinderGeometry(0.3, 0.3, 4, 16);
    const cylMesh1 = new THREE.Mesh(cylGeo, glowingHydraulic);
    cylMesh1.position.set(-1, 4, 0);
    boomPivot.add(cylMesh1);

    parts.push({
        name: "Hydraulic Cylinders",
        description: "Massive tubes containing highly pressurized hydraulic fluid (up to 5,000 psi).",
        material: "Chrome / Steel",
        function: "Converts fluid pressure from the engine pump into immense linear mechanical force to push and pull the heavy steel arms.",
        assemblyOrder: 4,
        connections: ["Boom", "Stick", "Bucket"],
        failureEffect: "Blown hydraulic hose.",
        cascadeFailures: ["Sudden loss of pressure", "Arm drops instantly", "Environmental fluid spill"],
        originalPosition: {x:0, y:0, z:0},
        explodedPosition: {x:0, y:8, z:0}
    });

    const description = "Civil Excavator Arm: A masterpiece of hydraulic leverage. Using Pascal's Law, pressurized fluid generated by a diesel engine pump drives massive pistons, allowing a single operator to tear through solid earth and lift tons of rock with surgical precision.";

    const quizQuestions = [
        {
            question: "What physical law allows the relatively small hydraulic cylinders to exert massive digging forces?",
            options: ["Pascal's Law: Pressure applied to a confined fluid is transmitted equally in all directions, multiplying force based on the area of the piston", "Newton's Third Law", "The Law of Gravity", "Boyle's Law"],
            correct: 0,
            explanation: "By pumping high pressure (e.g., 5000 psi) into a large diameter cylinder, the force is multiplied. A 6-inch piston at 5000 psi generates over 140,000 pounds of pushing force.",
            difficulty: "Medium"
        },
        {
            question: "Why do excavator buckets have large, replaceable 'teeth' on the cutting edge?",
            options: ["To shatter and penetrate hard soil, rock, and roots by concentrating the massive hydraulic force onto a very small surface area", "To look aggressive", "To filter out sand", "To grip the dirt so it doesn't fall out"],
            correct: 0,
            explanation: "Force = Pressure x Area. By focusing 100,000 pounds of force onto the tiny tips of 5 hardened teeth, the extreme localized pressure easily shatters solid rock and cuts through thick tree roots.",
            difficulty: "Easy"
        },
        {
            question: "What is the most dangerous failure mode for an excavator working on a steep hill?",
            options: ["A blown hydraulic line", "Running out of fuel", "The radio breaking", "A scratched window"],
            correct: 0,
            explanation: "The boom is held up entirely by trapped hydraulic pressure. If a heavy-duty hose bursts, all pressure is lost instantly, and the massive arm (and load) will drop out of the sky in a fraction of a second.",
            difficulty: "Hard"
        }
    ];

    function animate(time, speed, meshes) {
        // Digging cycle animation
        const cycle = time * speed;
        
        if (group.children[1]) {
            const boomPivot = group.children[1];
            const stickPivot = boomPivot.children[1];
            const bucketPivot = stickPivot.children[1];
            
            // Boom goes down and up
            boomPivot.rotation.z = Math.PI / 6 + Math.sin(cycle) * 0.2;
            
            // Stick reaches out and pulls in
            stickPivot.rotation.z = -Math.PI / 3 + Math.cos(cycle) * 0.4;
            
            // Bucket curls
            bucketPivot.rotation.z = Math.PI / 4 + Math.sin(cycle + Math.PI/2) * 0.5;
            
            // Pulse hydraulics
            const cyl = boomPivot.children[2];
            if(cyl) cyl.material.emissiveIntensity = 1.0 + Math.abs(Math.sin(cycle*2));
        }
    }

    return { group, parts, description, quizQuestions, animate };
}

