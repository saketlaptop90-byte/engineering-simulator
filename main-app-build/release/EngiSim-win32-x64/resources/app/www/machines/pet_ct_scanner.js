export function createPETCTScanner(THREE) {
    const machine = new THREE.Group();
    const parts = [];

    // 1. Gantry Housing
    const gantryShape = new THREE.Shape();
    gantryShape.moveTo(4, 0);
    gantryShape.absarc(0, 0, 4, 0, Math.PI * 2, false);

    const boreHole = new THREE.Path();
    boreHole.moveTo(1.5, 0);
    boreHole.absarc(0, 0, 1.5, 0, Math.PI * 2, true);
    gantryShape.holes.push(boreHole);

    const gantryGeom = new THREE.ExtrudeGeometry(gantryShape, { 
        depth: 2.5, 
        curveSegments: 32, 
        bevelEnabled: true, 
        bevelThickness: 0.1, 
        bevelSize: 0.1, 
        bevelSegments: 3 
    });
    const gantryMat = new THREE.MeshStandardMaterial({ 
        color: 0xe0e0e0, 
        roughness: 0.5, 
        metalness: 0.2,
        transparent: true,
        opacity: 0.25
    });
    const gantryHousing = new THREE.Mesh(gantryGeom, gantryMat);
    gantryHousing.position.set(0, 4, -1.25);
    machine.add(gantryHousing);
    parts.push({ name: "Gantry Housing", description: "Encloses the internal components of both the PET and CT scanners, maintaining a controlled environment." });

    // 2. Patient Bore
    const boreGeom = new THREE.CylinderGeometry(1.49, 1.49, 3.2, 32, 1, true);
    const boreMat = new THREE.MeshStandardMaterial({ 
        color: 0xaaaaaa, 
        roughness: 0.3, 
        side: THREE.DoubleSide,
        transparent: true,
        opacity: 0.3
    });
    const patientBore = new THREE.Mesh(boreGeom, boreMat);
    patientBore.rotation.x = Math.PI / 2;
    patientBore.position.set(0, 4, 0);
    machine.add(patientBore);
    parts.push({ name: "Patient Bore", description: "The opening through which the patient passes during the scanning process." });

    // Internal Rotor for CT & X-ray & PET
    const rotor = new THREE.Group();
    rotor.position.set(0, 4, 0);
    machine.add(rotor);

    // 3. PET Detectors
    const petDetectors = new THREE.Group();
    const petMat = new THREE.MeshStandardMaterial({ color: 0x4488ff, roughness: 0.4, metalness: 0.8 });
    for(let i=0; i<36; i++) {
        const d = new THREE.Mesh(new THREE.BoxGeometry(0.2, 0.4, 0.8), petMat);
        const angle = (i / 36) * Math.PI * 2;
        d.position.set(Math.cos(angle) * 1.8, Math.sin(angle) * 1.8, -0.5);
        d.rotation.z = angle;
        petDetectors.add(d);
    }
    rotor.add(petDetectors);
    parts.push({ name: "PET Detectors", description: "Ring of scintillation crystals that detect pairs of gamma rays emitted indirectly by a positron-emitting radiotracer." });

    // 4. X-Ray Tube
    const xrayGeom = new THREE.CylinderGeometry(0.3, 0.3, 0.8, 16);
    const xrayMat = new THREE.MeshStandardMaterial({ color: 0xdd4444, metalness: 0.6, roughness: 0.3 });
    const xrayTube = new THREE.Mesh(xrayGeom, xrayMat);
    xrayTube.position.set(0, 2.3, 0.5);
    xrayTube.rotation.z = Math.PI / 2;
    rotor.add(xrayTube);
    parts.push({ name: "X-Ray Tube", description: "Generates X-rays that pass through the patient to create cross-sectional CT images for anatomical reference." });

    // 5. CT Detectors
    const ctDetectors = new THREE.Group();
    const ctMat = new THREE.MeshStandardMaterial({ color: 0x44cc44, roughness: 0.5, metalness: 0.4 });
    for(let i=-5; i<=5; i++) {
        const d = new THREE.Mesh(new THREE.BoxGeometry(0.15, 0.1, 0.6), ctMat);
        const angle = (i / 15) * Math.PI - Math.PI/2;
        d.position.set(Math.cos(angle) * 2.3, Math.sin(angle) * 2.3, 0.5);
        d.rotation.z = angle;
        ctDetectors.add(d);
    }
    rotor.add(ctDetectors);
    parts.push({ name: "CT Detectors", description: "Measures the attenuation of X-rays passing through the body to construct high-resolution anatomical images." });

    // 6. Cooling System
    const coolingGeom = new THREE.BoxGeometry(2, 1, 1.5);
    const coolingMat = new THREE.MeshStandardMaterial({ color: 0x555555, roughness: 0.7 });
    const coolingSystem = new THREE.Mesh(coolingGeom, coolingMat);
    coolingSystem.position.set(0, 8.5, 0);
    machine.add(coolingSystem);
    parts.push({ name: "Cooling System", description: "Dissipates the intense heat generated by the X-ray tube and electronic components during continuous operation." });

    // 7. Patient Bed
    const bedGroup = new THREE.Group();
    const baseGeom = new THREE.BoxGeometry(1.5, 2, 4);
    const baseMat = new THREE.MeshStandardMaterial({ color: 0x999999 });
    const bedBase = new THREE.Mesh(baseGeom, baseMat);
    bedBase.position.set(0, 1, 5);
    machine.add(bedBase);

    const palletGeom = new THREE.BoxGeometry(1.2, 0.1, 6);
    const palletMat = new THREE.MeshStandardMaterial({ color: 0x333333 });
    const patientBed = new THREE.Mesh(palletGeom, palletMat);
    patientBed.position.set(0, 2.05, 5);
    machine.add(patientBed);
    
    parts.push({ name: "Patient Bed", description: "Motorized table that smoothly moves the patient through the bore to capture images at different positions." });

    // 8. Cyclotron Target
    const cyclotronGroup = new THREE.Group();
    const cycBody = new THREE.Mesh(
        new THREE.CylinderGeometry(1.5, 1.5, 2.5, 32),
        new THREE.MeshStandardMaterial({color: 0xaaaa00, metalness: 0.5})
    );
    cycBody.position.set(-6, 1.5, -4);
    cycBody.rotation.x = Math.PI / 2;
    cyclotronGroup.add(cycBody);
    machine.add(cyclotronGroup);
    parts.push({ name: "Cyclotron Target", description: "A separate particle accelerator unit used to produce short-lived positron-emitting isotopes (like FDG) for PET imaging." });

    // 9. Coincidence Processing Unit
    const cpuGeom = new THREE.BoxGeometry(1.5, 3, 1.5);
    const cpuMat = new THREE.MeshStandardMaterial({ color: 0x222222, roughness: 0.8 });
    const cpu = new THREE.Mesh(cpuGeom, cpuMat);
    cpu.position.set(6, 1.5, -3);
    
    const cpuLight = new THREE.Mesh(
        new THREE.BoxGeometry(1.51, 0.2, 0.2),
        new THREE.MeshBasicMaterial({ color: 0x00ff00 })
    );
    cpuLight.position.set(0, 1, 0.7);
    cpu.add(cpuLight);
    machine.add(cpu);
    parts.push({ name: "Coincidence Processing Unit", description: "High-speed electronics that identify simultaneous gamma ray hits to map positron annihilations accurately." });

    // 10. Console
    const consoleGroup = new THREE.Group();
    const desk = new THREE.Mesh(new THREE.BoxGeometry(3, 0.1, 1.5), new THREE.MeshStandardMaterial({color: 0x888888}));
    desk.position.set(6, 1.5, 4);
    const monitor1 = new THREE.Mesh(new THREE.BoxGeometry(0.8, 0.5, 0.05), new THREE.MeshStandardMaterial({color: 0x111111}));
    monitor1.position.set(5.5, 2, 3.8);
    monitor1.rotation.x = 0.1;
    const monitor2 = new THREE.Mesh(new THREE.BoxGeometry(0.8, 0.5, 0.05), new THREE.MeshStandardMaterial({color: 0x111111}));
    monitor2.position.set(6.5, 2, 3.8);
    monitor2.rotation.x = 0.1;
    consoleGroup.add(desk);
    consoleGroup.add(monitor1);
    consoleGroup.add(monitor2);
    machine.add(consoleGroup);
    parts.push({ name: "Console", description: "Workstation where technologists control the scanner, monitor the patient, and process the acquired hybrid images." });

    machine.parts = parts;

    // Animation
    machine.animation = (time) => {
        // Rotate the internal rotor
        rotor.rotation.z = time * 2;
        
        // Bed slides back and forth smoothly
        const slide = Math.sin(time * 0.5); // ranges from -1 to 1
        patientBed.position.z = 5 - (slide + 1) * 2; // slides from z=5 to z=1
    };

    // Quiz
    machine.quiz = [
        {
            question: "What is the primary function of the PET component in a PET-CT scanner?",
            options: [
                "To provide high-resolution anatomical details",
                "To detect metabolic and biochemical activity",
                "To measure bone density",
                "To generate a strong magnetic field"
            ],
            answer: 1
        },
        {
            question: "Which of the following is commonly used as a radiotracer in oncology PET scans?",
            options: [
                "Barium sulfate",
                "Iodine-131",
                "Fluorodeoxyglucose (FDG)",
                "Gadolinium"
            ],
            answer: 2
        },
        {
            question: "What is the role of the CT scan in a combined PET-CT imaging system?",
            options: [
                "It detects positron annihilations",
                "It produces the radiotracer",
                "It provides anatomical reference and attenuation correction",
                "It measures the half-life of isotopes"
            ],
            answer: 2
        },
        {
            question: "What physical event does the Coincidence Processing Unit specifically look for?",
            options: [
                "Two gamma rays striking detectors almost simultaneously at 180 degrees",
                "Single X-ray photons passing through the patient",
                "Protons aligning with a magnetic field",
                "The decay of alpha particles"
            ],
            answer: 0
        },
        {
            question: "Why is a cyclotron necessary for many PET imaging facilities?",
            options: [
                "To shield the patient from radiation",
                "To generate X-rays for the CT scan",
                "To cool down the PET detectors",
                "To produce short-lived positron-emitting isotopes"
            ],
            answer: 3
        },
        {
            question: "What does the combination of PET and CT images (image fusion) primarily improve?",
            options: [
                "The scan speed",
                "The spatial localization of metabolic abnormalities",
                "The comfort of the patient bed",
                "The reduction of scanner noise"
            ],
            answer: 1
        }
    ];

    return machine;
}
