export function createDopplerWeatherRadar(THREE) {
    const group = new THREE.Group();

    const parts = [
        { name: "Support Tower", description: "Provides elevation to the radar system to clear surrounding obstacles and increase range." },
        { name: "Pedestal", description: "Houses the motors and gears that rotate and elevate the antenna." },
        { name: "Transmitter", description: "Generates high-power microwave pulses that are sent out by the antenna." },
        { name: "Receiver", description: "Detects the weak echoes returning from precipitation and other targets." },
        { name: "Waveguide", description: "Hollow metal tubes that guide microwave energy between the transmitter/receiver and the antenna." },
        { name: "Cooling System", description: "Dissipates the immense heat generated by the high-power transmitter." },
        { name: "Generator", description: "Provides backup electrical power to ensure continuous operation during outages." },
        { name: "Data Processor", description: "Analyzes raw signals to determine reflectivity, velocity (Doppler shift), and spectrum width." },
        { name: "Dish Antenna", description: "Focuses the microwave energy into a narrow beam and collects the returning echoes." },
        { name: "Radome", description: "A weatherproof, dome-shaped enclosure that protects the antenna from wind, ice, and debris." }
    ];

    // Materials
    const towerMat = new THREE.MeshStandardMaterial({ color: 0x888888 });
    const pedestalMat = new THREE.MeshStandardMaterial({ color: 0x555555 });
    const componentMat = new THREE.MeshStandardMaterial({ color: 0x334455 });
    const waveguideMat = new THREE.MeshStandardMaterial({ color: 0xb87333 }); // Copper
    const antennaMat = new THREE.MeshStandardMaterial({ color: 0xdddddd, side: THREE.DoubleSide });
    const radomeMat = new THREE.MeshStandardMaterial({ color: 0xffffff, transparent: true, opacity: 0.4 });
    const baseMat = new THREE.MeshStandardMaterial({ color: 0x444444 });

    // 1. Support Tower
    const towerGeo = new THREE.CylinderGeometry(2, 3, 15, 8);
    const tower = new THREE.Mesh(towerGeo, towerMat);
    tower.position.y = 7.5;
    group.add(tower);

    // 2. Pedestal
    const pedestalGeo = new THREE.CylinderGeometry(1.5, 2, 2, 8);
    const pedestal = new THREE.Mesh(pedestalGeo, pedestalMat);
    pedestal.position.y = 16;
    group.add(pedestal);

    // 3. Transmitter
    const txGeo = new THREE.BoxGeometry(1.5, 3, 1.5);
    const transmitter = new THREE.Mesh(txGeo, componentMat);
    transmitter.position.set(1.5, 12, 0);
    group.add(transmitter);

    // 4. Receiver
    const rxGeo = new THREE.BoxGeometry(1.5, 2, 1.5);
    const receiver = new THREE.Mesh(rxGeo, componentMat);
    receiver.position.set(-1.5, 12.5, 0);
    group.add(receiver);

    // 5. Waveguide
    const wgGeo = new THREE.CylinderGeometry(0.2, 0.2, 4, 8);
    const waveguide = new THREE.Mesh(wgGeo, waveguideMat);
    waveguide.position.set(0, 14, 0);
    group.add(waveguide);

    // 6. Cooling System
    const coolingGeo = new THREE.BoxGeometry(2, 2, 1);
    const coolingSystem = new THREE.Mesh(coolingGeo, new THREE.MeshStandardMaterial({ color: 0x222222 }));
    coolingSystem.position.set(0, 8, 2.5);
    group.add(coolingSystem);

    // 7. Generator
    const genGeo = new THREE.BoxGeometry(3, 2, 4);
    const generator = new THREE.Mesh(genGeo, baseMat);
    generator.position.set(4, 1, 0);
    group.add(generator);

    // 8. Data Processor
    const dpGeo = new THREE.BoxGeometry(2, 3, 2);
    const dataProcessor = new THREE.Mesh(dpGeo, baseMat);
    dataProcessor.position.set(-4, 1.5, 0);
    group.add(dataProcessor);

    // We will animate the rotating assembly
    const rotatingAssembly = new THREE.Group();
    rotatingAssembly.position.y = 17;
    group.add(rotatingAssembly);

    // 9. Dish Antenna
    // Using a sphere segment to represent a parabolic dish
    const antennaGeo = new THREE.SphereGeometry(3, 32, 16, 0, Math.PI * 2, 0, Math.PI / 4);
    const dishAntenna = new THREE.Mesh(antennaGeo, antennaMat);
    dishAntenna.rotation.x = Math.PI / 2; // point outwards
    // move dish slightly to the side to offset its center of rotation
    dishAntenna.position.z = 1;
    rotatingAssembly.add(dishAntenna);

    const dishSupportGeo = new THREE.BoxGeometry(0.5, 2, 1);
    const dishSupport = new THREE.Mesh(dishSupportGeo, pedestalMat);
    dishSupport.position.z = -1;
    rotatingAssembly.add(dishSupport);

    // 10. Radome
    const radomeGeo = new THREE.SphereGeometry(5, 32, 32);
    const radome = new THREE.Mesh(radomeGeo, radomeMat);
    // Radome is fixed, covers the rotating assembly
    radome.position.y = 17;
    group.add(radome);

    const animate = function(delta) {
        if (rotatingAssembly) {
            rotatingAssembly.rotation.y -= 1.5 * delta; // Continuous rotation
        }
    };

    const questions = [
        {
            question: "What is the primary function of the Transmitter in a Doppler weather radar?",
            options: [
                "To interpret the incoming data and produce maps.",
                "To generate high-power microwave pulses.",
                "To rotate the antenna continuously.",
                "To protect the system from wind and rain."
            ],
            correctAnswer: 1
        },
        {
            question: "How does the radar measure the velocity of precipitation (Doppler effect)?",
            options: [
                "By measuring the physical weight of raindrops.",
                "By taking a photograph of the storm clouds.",
                "By measuring the frequency shift in the returned signal.",
                "By detecting the temperature of the air."
            ],
            correctAnswer: 2
        },
        {
            question: "What is the purpose of the Radome?",
            options: [
                "To protect the antenna from wind, ice, and debris.",
                "To increase the power of the microwave pulses.",
                "To generate backup electrical power.",
                "To cool down the transmitter."
            ],
            correctAnswer: 0
        },
        {
            question: "Which component guides microwave energy between the transmitter/receiver and the antenna?",
            options: [
                "Data Processor",
                "Cooling System",
                "Waveguide",
                "Support Tower"
            ],
            correctAnswer: 2
        },
        {
            question: "Why does the radar have a Cooling System?",
            options: [
                "To freeze incoming precipitation for easier detection.",
                "To dissipate immense heat generated by the high-power transmitter.",
                "To keep the radome from melting.",
                "To provide air conditioning for the data processors."
            ],
            correctAnswer: 1
        },
        {
            question: "What role does the Pedestal play in the radar system?",
            options: [
                "It serves as a backup power source.",
                "It houses motors and gears that rotate and elevate the antenna.",
                "It processes all incoming signal data.",
                "It acts as the primary receiver for radar echoes."
            ],
            correctAnswer: 1
        }
    ];

    return {
        group,
        parts,
        animate,
        questions
    };
}
