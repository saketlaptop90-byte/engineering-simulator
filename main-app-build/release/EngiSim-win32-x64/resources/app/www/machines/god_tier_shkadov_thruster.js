    flareGeo.translate(0, 50, 0); // Origin at base
    const flareMat = new THREE.MeshStandardMaterial({
        color: 0xffddaa,
        emissive: 0xff5500,
        emissiveIntensity: 8.0,
        transparent: true,
        opacity: 0.8,
        depthWrite: false,
        blending: THREE.AdditiveBlending
    });
    const flareInstanced = new THREE.InstancedMesh(flareGeo, flareMat, NUM_SOLAR_FLARES);
    flareInstanced.instanceMatrix.setUsage(THREE.DynamicDrawUsage);
    
    for (let i = 0; i < NUM_SOLAR_FLARES; i++) {
        ANIMATION_STATE.flareData.push({
            lat: (Math.random() - 0.5) * Math.PI,
            lon: Math.random() * Math.PI * 2,
            height: Math.random() * 300 + 100,
            phase: Math.random() * Math.PI * 2,
            speed: Math.random() * 0.05 + 0.02
        });
    }
    starGroup.add(flareInstanced);

    parts.push({
        name: "Solar Flare Plumes",
        description: "Violent eruptions of plasma guided by magnetic field lines, safely deflected by the megastructure.",
        material: "Plasma and Photons",
        function: "Energy exhaustion and pressure release from the stellar core.",
        assemblyOrder: 4,
        connections: ["Chromosphere"],
        failureEffect: "Direct impact on statite mirror",
        cascadeFailures: ["Mirror ablation", "Catastrophic statite failure"],
        originalPosition: { x: 0, y: 0, z: 0 },
        explodedPosition: { x: 0, y: 500, z: -800 }
    });

    // 3. THE SHKADOV MIRROR (Statite)
    const mirrorGroup = new THREE.Group();
    // Offset the mirror so the star is at its focal point/center
    mirrorGroup.position.set(0, 0, 0);
    group.add(mirrorGroup);

    // Inner Reflective Hemisphere
