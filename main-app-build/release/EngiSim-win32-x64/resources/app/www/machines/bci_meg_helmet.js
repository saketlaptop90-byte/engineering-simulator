import { plastic, aluminum, glass, copper, steel, darkSteel, rubber, chrome, tinted } from '../utils/materials.js';

export function createMachine(THREE) {
    const group = new THREE.Group();
    const parts = [];

    // Custom Materials
    const squidGlow = new THREE.MeshStandardMaterial({ 
        color: 0x00ffff, 
        emissive: 0x00ffff, 
        emissiveIntensity: 0.8, 
        roughness: 0.2, 
        metalness: 0.8,
        transparent: true,
        opacity: 0.9
    });
    const fieldGlow = new THREE.MeshStandardMaterial({
        color: 0xff00ff,
        emissive: 0xff00ff,
        emissiveIntensity: 0.5,
        transparent: true,
        opacity: 0.4,
        wireframe: true
    });
    const coolingLiquid = new THREE.MeshStandardMaterial({
        color: 0x0055ff,
        transparent: true,
        opacity: 0.6,
        roughness: 0.1,
        metalness: 0.1
    });

    // 1. Outer Shell
    const shellGeo = new THREE.SphereGeometry(1.5, 32, 32, 0, Math.PI * 2, 0, Math.PI * 0.6);
    const shell = new THREE.Mesh(shellGeo, plastic);
    shell.position.set(0, 0, 0);
    shell.name = "Outer Shielding Shell";
    group.add(shell);
    parts.push({
        name: "Outer Shielding Shell",
        description: "Mu-metal and aluminum alloy shell providing magnetic shielding from Earth's magnetic field and environmental noise.",
        material: "Mu-Metal / Aluminum",
        function: "Shields internal sensors from external magnetic interference.",
        assemblyOrder: 5,
        connections: ["Cooling Dewar", "Structural Frame"],
        failureEffect: "External magnetic noise completely overwhelms the weak brain signals.",
        cascadeFailures: ["Signal Processing Unit"],
        originalPosition: { x: 0, y: 0, z: 0 },
        explodedPosition: { x: 0, y: 3, z: 0 }
    });

    // 2. Cooling Dewar
    const dewarGeo = new THREE.SphereGeometry(1.3, 32, 32, 0, Math.PI * 2, 0, Math.PI * 0.58);
    const dewar = new THREE.Mesh(dewarGeo, chrome);
    dewar.position.set(0, 0.1, 0);
    dewar.name = "Liquid Helium Dewar";
    group.add(dewar);
    parts.push({
        name: "Liquid Helium Dewar",
        description: "Vacuum-insulated vessel containing liquid helium to cool the SQUIDs.",
        material: "Chrome / Glass Vacuum",
        function: "Maintains a temperature of 4 Kelvin to allow superconductivity.",
        assemblyOrder: 3,
        connections: ["SQUID Array", "Outer Shielding Shell"],
        failureEffect: "Loss of superconductivity, sensors stop functioning instantly.",
        cascadeFailures: ["SQUID Array", "Superconducting Coils"],
        originalPosition: { x: 0, y: 0.1, z: 0 },
        explodedPosition: { x: 0, y: 2, z: 0 }
    });

    // 3. SQUID Array (Sensors)
    const squidGroup = new THREE.Group();
    squidGroup.name = "SQUID Array";
    
    const numSensors = 100;
    const radius = 1.15;
    for (let i = 0; i < numSensors; i++) {
        const phi = Math.acos(1 - 2 * (i + 0.5) / numSensors);
        const theta = Math.PI * (1 + Math.sqrt(5)) * i;
        if (phi < Math.PI * 0.55) { // Only top half roughly
            const sensorGeo = new THREE.CylinderGeometry(0.04, 0.04, 0.15, 8);
            const sensor = new THREE.Mesh(sensorGeo, squidGlow);
            const x = radius * Math.sin(phi) * Math.cos(theta);
            const y = radius * Math.cos(phi);
            const z = radius * Math.sin(phi) * Math.sin(theta);
            sensor.position.set(x, y, z);
            sensor.lookAt(0, 0, 0);
            sensor.rotateX(Math.PI / 2);
            squidGroup.add(sensor);
        }
    }
    squidGroup.position.set(0, 0.15, 0);
    group.add(squidGroup);
    parts.push({
        name: "SQUID Array",
        description: "Superconducting Quantum Interference Devices arranged in a dense array to measure femtotesla magnetic fields.",
        material: "Niobium / Superconductor",
        function: "Detects extremely weak magnetic fields generated by neural activity.",
        assemblyOrder: 4,
        connections: ["Liquid Helium Dewar", "Data Acquisition Interface"],
        failureEffect: "Loss of signal detection capability.",
        cascadeFailures: ["Data Acquisition Interface"],
        originalPosition: { x: 0, y: 0.15, z: 0 },
        explodedPosition: { x: 0, y: 0.5, z: 0 }
    });

    // 4. Flux Transformers (Superconducting Coils)
    const coilsGroup = new THREE.Group();
    coilsGroup.name = "Flux Transformers";
    for(let i=0; i < 30; i++) {
        const phi = Math.random() * Math.PI * 0.5;
        const theta = Math.random() * Math.PI * 2;
        const r = 1.05;
        const x = r * Math.sin(phi) * Math.cos(theta);
        const y = r * Math.cos(phi);
        const z = r * Math.sin(phi) * Math.sin(theta);
        
        const coilGeo = new THREE.TorusGeometry(0.06, 0.015, 8, 16);
        const coil = new THREE.Mesh(coilGeo, copper);
        coil.position.set(x, y, z);
        coil.lookAt(0, 0, 0);
        coilsGroup.add(coil);
    }
    coilsGroup.position.set(0, 0.15, 0);
    group.add(coilsGroup);
    parts.push({
        name: "Flux Transformers",
        description: "Superconducting pick-up coils that couple the magnetic field from the brain into the SQUIDs.",
        material: "Copper / Superconductor Wire",
        function: "Gathers and concentrates neural magnetic flux.",
        assemblyOrder: 2,
        connections: ["SQUID Array", "Inner Helmet Surface"],
        failureEffect: "Reduced sensitivity or loss of spatial resolution in specific channels.",
        cascadeFailures: ["Signal Processing Unit"],
        originalPosition: { x: 0, y: 0.15, z: 0 },
        explodedPosition: { x: 0, y: -0.5, z: 0 }
    });

    // 5. Inner Head padding
    const paddingGeo = new THREE.SphereGeometry(0.95, 32, 32, 0, Math.PI * 2, 0, Math.PI * 0.55);
    const padding = new THREE.Mesh(paddingGeo, rubber);
    padding.position.set(0, 0.2, 0);
    padding.name = "Head Interface Padding";
    // invert geometry
    paddingGeo.scale(-1, 1, 1);
    group.add(padding);
    parts.push({
        name: "Head Interface Padding",
        description: "Ergonomic foam padding ensuring the subject's head remains perfectly still and comfortably positioned.",
        material: "Anti-static Foam / Rubber",
        function: "Stabilizes the head and reduces movement artifacts.",
        assemblyOrder: 1,
        connections: ["Flux Transformers"],
        failureEffect: "Patient movement introduces massive artifacts, ruining the scan.",
        cascadeFailures: [],
        originalPosition: { x: 0, y: 0.2, z: 0 },
        explodedPosition: { x: 0, y: -1.5, z: 0 }
    });

    // 6. Data Acquisition Cables
    const cableGeo = new THREE.CylinderGeometry(0.1, 0.1, 2, 16);
    const cable = new THREE.Mesh(cableGeo, darkSteel);
    cable.position.set(0, 1.5, -1);
    cable.rotation.x = Math.PI / 4;
    cable.name = "Data Interface Cables";
    group.add(cable);
    parts.push({
        name: "Data Interface Cables",
        description: "Fiber-optic and heavily shielded electrical cables transmitting multiplexed sensor data.",
        material: "Dark Steel / Fiber Optics",
        function: "Carries raw SQUID signals to the external processing unit.",
        assemblyOrder: 6,
        connections: ["Outer Shielding Shell", "Signal Processing Unit"],
        failureEffect: "Total loss of communication with the helmet.",
        cascadeFailures: ["Control Workstation"],
        originalPosition: { x: 0, y: 1.5, z: -1 },
        explodedPosition: { x: 0, y: 2.5, z: -2 }
    });

    // 7. Visualized Magnetic Fields (Decorative)
    const fieldGeo = new THREE.TorusGeometry(1.6, 0.05, 16, 64);
    const fieldMesh = new THREE.Mesh(fieldGeo, fieldGlow);
    fieldMesh.position.set(0, 0.5, 0);
    fieldMesh.rotation.x = Math.PI / 2;
    fieldMesh.name = "Magnetic Field Visualizer";
    group.add(fieldMesh);

    const description = "The Magnetoencephalography (MEG) Helmet is a highly advanced neuroimaging interface. It uses arrays of Superconducting Quantum Interference Devices (SQUIDs) bathed in liquid helium to detect the infinitesimal magnetic fields produced by electrical currents in the brain. MEG offers exceptional temporal and spatial resolution.";

    const quizQuestions = [
        {
            question: "What does SQUID stand for in the context of MEG technology?",
            options: [
                "Superconducting Quantum Interference Device",
                "Sensory Quantitative Interpretation Driver",
                "Signal Quality Under Intense Demagnetization",
                "Spatial Quantification of Intersecting Data"
            ],
            correct: 0,
            explanation: "SQUIDs are ultra-sensitive magnetometers used to measure extremely subtle magnetic fields, like those generated by neural activity.",
            difficulty: "Medium"
        },
        {
            question: "Why does the MEG system require liquid helium?",
            options: [
                "To clean the optical sensors",
                "To maintain the SQUIDs at a superconducting temperature (~4K)",
                "To conduct electrical signals to the patient's scalp",
                "To provide structural support to the heavy helmet"
            ],
            correct: 1,
            explanation: "SQUIDs only operate in a superconducting state, which requires extreme cold. Liquid helium provides temperatures near absolute zero.",
            difficulty: "Medium"
        },
        {
            question: "What is a primary advantage of MEG over traditional EEG?",
            options: [
                "It is much cheaper to operate",
                "It is portable and can be worn while walking",
                "Magnetic fields are significantly less distorted by the skull and scalp",
                "It directly measures blood flow instead of electrical activity"
            ],
            correct: 2,
            explanation: "Unlike electrical potentials measured by EEG, magnetic fields pass through tissue and bone with minimal distortion, offering superior spatial resolution.",
            difficulty: "Hard"
        }
    ];

    function animate(time, speed, meshes) {
        // Pulsate SQUID sensors
        const squidIntensity = 0.5 + 0.5 * Math.sin(time * speed * 2);
        squidGlow.emissiveIntensity = squidIntensity;

        // Rotate magnetic field visualizer
        const fieldMesh = meshes.find(m => m.name === "Magnetic Field Visualizer");
        if (fieldMesh) {
            fieldMesh.rotation.z += 0.02 * speed;
            fieldMesh.scale.setScalar(1 + 0.05 * Math.sin(time * speed * 0.5));
            fieldGlow.emissiveIntensity = 0.3 + 0.2 * Math.cos(time * speed);
        }

        // Slight hum/vibration on the dewar
        const dewar = meshes.find(m => m.name === "Liquid Helium Dewar");
        if (dewar) {
            dewar.position.y = 0.1 + Math.sin(time * speed * 10) * 0.002;
        }
    }

    return {
        group,
        parts,
        description,
        quizQuestions,
        animate
    };
}

