export function createPolarWeatherSatellite(THREE) {
    const group = new THREE.Group();

    const parts = [
        { name: "Main Bus", description: "The central body of the satellite housing the main electronics and systems." },
        { name: "Solar Array", description: "Generates power for the satellite from sunlight." },
        { name: "Microwave Sounder", description: "Measures atmospheric temperature and humidity profiles, even through clouds." },
        { name: "Infrared Imager", description: "Captures high-resolution images of clouds, land, and sea surfaces based on heat emissions." },
        { name: "Radiator Panels", description: "Dissipates excess heat generated by the satellite's instruments into space." },
        { name: "Star Tracker", description: "Determines the satellite's orientation (attitude) by observing the positions of stars." },
        { name: "Communications Antenna", description: "Transmits collected weather data back to Earth stations." },
        { name: "Propulsion Thruster", description: "Used for orbital adjustments and maintaining the correct altitude." },
        { name: "Battery Pack", description: "Stores energy generated by the solar array for use during the orbital eclipse (when in Earth's shadow)." },
        { name: "Sun Sensor", description: "Detects the position of the sun to help orient the solar arrays for maximum efficiency." }
    ];

    // 1. Main Bus
    const busGeometry = new THREE.BoxGeometry(2, 3, 2);
    const busMaterial = new THREE.MeshStandardMaterial({ color: 0xcccccc, metalness: 0.5, roughness: 0.5 });
    const mainBus = new THREE.Mesh(busGeometry, busMaterial);
    group.add(mainBus);

    // 2. Solar Array
    const solarArrayGroup = new THREE.Group();
    const solarPanelGeometry = new THREE.BoxGeometry(6, 0.1, 1.5);
    const solarPanelMaterial = new THREE.MeshStandardMaterial({ color: 0x112266, metalness: 0.8, roughness: 0.2 });
    const solarPanel1 = new THREE.Mesh(solarPanelGeometry, solarPanelMaterial);
    solarPanel1.position.set(4, 0, 0);
    const solarPanel2 = new THREE.Mesh(solarPanelGeometry, solarPanelMaterial);
    solarPanel2.position.set(-4, 0, 0);
    solarArrayGroup.add(solarPanel1);
    solarArrayGroup.add(solarPanel2);
    group.add(solarArrayGroup);

    // 3. Microwave Sounder
    const sounderGeometry = new THREE.CylinderGeometry(0.5, 0.5, 1, 32);
    const sounderMaterial = new THREE.MeshStandardMaterial({ color: 0xaaaaaa, metalness: 0.6, roughness: 0.4 });
    const microwaveSounder = new THREE.Mesh(sounderGeometry, sounderMaterial);
    microwaveSounder.position.set(0, -1.5, 1);
    microwaveSounder.rotation.x = Math.PI / 2;
    group.add(microwaveSounder);

    // 4. Infrared Imager
    const imagerGeometry = new THREE.BoxGeometry(0.8, 0.8, 1);
    const imagerMaterial = new THREE.MeshStandardMaterial({ color: 0x333333 });
    const infraredImager = new THREE.Mesh(imagerGeometry, imagerMaterial);
    infraredImager.position.set(0, -1.5, -1);
    // Lens
    const lensGeometry = new THREE.CylinderGeometry(0.3, 0.3, 0.2, 32);
    const lensMaterial = new THREE.MeshStandardMaterial({ color: 0x000000, metalness: 0.9, roughness: 0.1 });
    const lens = new THREE.Mesh(lensGeometry, lensMaterial);
    lens.position.set(0, 0, -0.6);
    lens.rotation.x = Math.PI / 2;
    infraredImager.add(lens);
    group.add(infraredImager);

    // 5. Radiator Panels
    const radiatorGeometry = new THREE.BoxGeometry(1.8, 2.8, 0.1);
    const radiatorMaterial = new THREE.MeshStandardMaterial({ color: 0xffffff, metalness: 0.2, roughness: 0.8 });
    const radiator = new THREE.Mesh(radiatorGeometry, radiatorMaterial);
    radiator.position.set(0, 0, 1.05);
    group.add(radiator);

    // 6. Star Tracker
    const trackerGeometry = new THREE.ConeGeometry(0.2, 0.5, 16);
    const trackerMaterial = new THREE.MeshStandardMaterial({ color: 0x888888 });
    const starTracker = new THREE.Mesh(trackerGeometry, trackerMaterial);
    starTracker.position.set(0.8, 1.5, 0);
    starTracker.rotation.z = -Math.PI / 4;
    group.add(starTracker);

    // 7. Communications Antenna
    const antennaGeometry = new THREE.SphereGeometry(0.6, 32, 32, 0, Math.PI * 2, 0, Math.PI / 2);
    const antennaMaterial = new THREE.MeshStandardMaterial({ color: 0xffcc00, metalness: 0.7, roughness: 0.3, side: THREE.DoubleSide });
    const commsAntenna = new THREE.Mesh(antennaGeometry, antennaMaterial);
    commsAntenna.position.set(0, 1.5, 0);
    // Antenna Feed
    const feedGeometry = new THREE.CylinderGeometry(0.05, 0.05, 0.8);
    const feed = new THREE.Mesh(feedGeometry, trackerMaterial);
    feed.position.set(0, 0.4, 0);
    commsAntenna.add(feed);
    group.add(commsAntenna);

    // 8. Propulsion Thruster
    const thrusterGeometry = new THREE.CylinderGeometry(0.2, 0.4, 0.6, 16);
    const thrusterMaterial = new THREE.MeshStandardMaterial({ color: 0x444444, metalness: 0.8, roughness: 0.5 });
    const thruster = new THREE.Mesh(thrusterGeometry, thrusterMaterial);
    thruster.position.set(0, -1.8, 0);
    group.add(thruster);

    // 9. Battery Pack
    const batteryGeometry = new THREE.BoxGeometry(1.5, 0.5, 1.5);
    const batteryMaterial = new THREE.MeshStandardMaterial({ color: 0xdd9900 });
    const batteryPack = new THREE.Mesh(batteryGeometry, batteryMaterial);
    batteryPack.position.set(0, -0.8, 0);
    group.add(batteryPack);

    // 10. Sun Sensor
    const sensorGeometry = new THREE.SphereGeometry(0.1, 16, 16);
    const sensorMaterial = new THREE.MeshStandardMaterial({ color: 0xff0000, emissive: 0x550000 });
    const sunSensor = new THREE.Mesh(sensorGeometry, sensorMaterial);
    sunSensor.position.set(0, 1.6, 0.8);
    group.add(sunSensor);

    let time = 0;

    const animate = function(delta) {
        time += delta;
        // Slow orbital rotation
        group.rotation.y += 0.1 * delta;
        
        // Solar panel tracking (oscillating to simulate tracking the sun)
        solarArrayGroup.rotation.x = Math.sin(time * 0.5) * 0.5;
    };

    const questions = [
        {
            question: "What is the primary advantage of a polar-orbiting weather satellite?",
            options: [
                "It stays over the same spot on Earth continuously",
                "It provides global coverage as the Earth rotates beneath it",
                "It operates closer to the sun",
                "It requires less power than geostationary satellites"
            ],
            correctAnswer: 1
        },
        {
            question: "Which instrument can measure atmospheric profiles through clouds?",
            options: [
                "Infrared Imager",
                "Visible Light Camera",
                "Microwave Sounder",
                "Star Tracker"
            ],
            correctAnswer: 2
        },
        {
            question: "What does the star tracker do?",
            options: [
                "Detects incoming asteroids",
                "Measures solar radiation",
                "Determines the satellite's attitude (orientation)",
                "Provides propulsion"
            ],
            correctAnswer: 2
        },
        {
            question: "Why do satellites have radiator panels?",
            options: [
                "To collect solar energy",
                "To dissipate excess heat into space",
                "To communicate with Earth",
                "To shield against micrometeoroids"
            ],
            correctAnswer: 1
        },
        {
            question: "What is the purpose of the sun sensor?",
            options: [
                "To measure global warming",
                "To detect solar flares",
                "To help orient the solar arrays toward the sun",
                "To power the microwave sounder directly"
            ],
            correctAnswer: 2
        },
        {
            question: "What part of the satellite stores energy for use when the satellite is in Earth's shadow?",
            options: [
                "Solar Array",
                "Propulsion Thruster",
                "Main Bus",
                "Battery Pack"
            ],
            correctAnswer: 3
        }
    ];

    return {
        group,
        parts,
        animate,
        questions
    };
}
