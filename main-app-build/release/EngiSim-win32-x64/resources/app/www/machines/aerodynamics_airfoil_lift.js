import { plastic, aluminum, glass, copper, steel, darkSteel, rubber, chrome, tinted } from '../utils/materials.js';

export function createMachine(THREE) {
    const group = new THREE.Group();
    const parts = [];
    const meshes = {};

    // Custom Materials
    const neonBlue = new THREE.MeshPhysicalMaterial({
        color: 0x0088ff,
        emissive: 0x0088ff,
        emissiveIntensity: 2,
        transparent: true,
        opacity: 0.8,
        roughness: 0.2,
        metalness: 0.8
    });

    const airParticleMat = new THREE.MeshBasicMaterial({
        color: 0x00ffff,
        transparent: true,
        opacity: 0.6,
        blending: THREE.AdditiveBlending
    });

    const windTunnelGlass = new THREE.MeshPhysicalMaterial({
        color: 0xeef2ff,
        metalness: 0.1,
        roughness: 0.0,
        transmission: 0.95,
        thickness: 0.5,
        transparent: true,
        opacity: 0.4
    });

    // 1. Wind Tunnel Chamber
    const chamberGeo = new THREE.BoxGeometry(20, 8, 8);
    const chamberEdges = new THREE.EdgesGeometry(chamberGeo);
    const chamberLine = new THREE.LineSegments(chamberEdges, new THREE.LineBasicMaterial({ color: 0x888888 }));
    
    const chamberWall = new THREE.Mesh(chamberGeo, windTunnelGlass);
    chamberWall.add(chamberLine);
    group.add(chamberWall);

    parts.push({
        name: "Test Chamber",
        description: "Enclosed transparent section where airflow and airfoil interactions are observed.",
        material: "windTunnelGlass",
        function: "Contains the airflow and provides a controlled environment for aerodynamic testing.",
        assemblyOrder: 1,
        connections: ["Intake Fan", "Exhaust"],
        failureEffect: "Loss of pressure control, invalidating aerodynamic readings.",
        cascadeFailures: ["Sensor Miscalibration", "Flow Separation"],
        originalPosition: { x: 0, y: 0, z: 0 },
        explodedPosition: { x: 0, y: 5, z: 0 }
    });
    meshes.chamber = chamberWall;

    // 2. Airfoil (Wing section)
    // Creating a custom shape for airfoil (NACA-like profile)
    const airfoilShape = new THREE.Shape();
    airfoilShape.moveTo(3, 0);
    // Upper curve
    airfoilShape.quadraticCurveTo(1, 1.5, -3, 0);
    // Lower curve
    airfoilShape.quadraticCurveTo(1, -0.2, 3, 0);

    const extrudeSettings = {
        depth: 6,
        bevelEnabled: true,
        bevelSegments: 2,
        steps: 2,
        bevelSize: 0.05,
        bevelThickness: 0.05
    };
    
    const airfoilGeo = new THREE.ExtrudeGeometry(airfoilShape, extrudeSettings);
    airfoilGeo.center();
    const airfoil = new THREE.Mesh(airfoilGeo, aluminum);
    // Rotate to lie horizontally
    airfoil.rotation.y = Math.PI / 2;
    group.add(airfoil);

    parts.push({
        name: "Airfoil Profile",
        description: "The cross-sectional shape of the wing, designed to generate lift when exposed to airflow.",
        material: "aluminum",
        function: "Accelerates air over the top surface (lower pressure) compared to the bottom (higher pressure), creating lift.",
        assemblyOrder: 2,
        connections: ["Mounting Strut"],
        failureEffect: "Stall condition, loss of lift.",
        cascadeFailures: ["Turbulence Generation"],
        originalPosition: { x: 0, y: 0, z: 0 },
        explodedPosition: { x: 0, y: 0, z: 5 }
    });
    meshes.airfoil = airfoil;

    // 3. Mounting Strut & Force Sensors
    const strutGeo = new THREE.CylinderGeometry(0.2, 0.2, 4, 16);
    const strut = new THREE.Mesh(strutGeo, darkSteel);
    strut.position.set(0, -2, 0);
    group.add(strut);

    parts.push({
        name: "Force Balance Strut",
        description: "Support structure that holds the airfoil in the chamber and contains load cells.",
        material: "darkSteel",
        function: "Measures lift and drag forces generated by the airfoil.",
        assemblyOrder: 3,
        connections: ["Airfoil Profile", "Chamber Base"],
        failureEffect: "Inaccurate force measurements.",
        cascadeFailures: ["Data Corruption"],
        originalPosition: { x: 0, y: -2, z: 0 },
        explodedPosition: { x: 0, y: -5, z: 0 }
    });
    meshes.strut = strut;

    // 4. Intake Fan / Flow Straightener
    const fanHousingGeo = new THREE.CylinderGeometry(3.8, 3.8, 2, 32);
    const fanHousing = new THREE.Mesh(fanHousingGeo, steel);
    fanHousing.rotation.z = Math.PI / 2;
    fanHousing.position.set(-11, 0, 0);
    group.add(fanHousing);

    const fanHubGeo = new THREE.CylinderGeometry(0.8, 0.8, 2.2, 16);
    const fanHub = new THREE.Mesh(fanHubGeo, chrome);
    fanHub.rotation.z = Math.PI / 2;
    fanHousing.add(fanHub);

    meshes.blades = [];
    for(let i=0; i<6; i++) {
        const bladeGeo = new THREE.BoxGeometry(0.1, 3, 0.5);
        bladeGeo.translate(0, 1.5, 0);
        const blade = new THREE.Mesh(bladeGeo, darkSteel);
        blade.rotation.x = (i * Math.PI * 2) / 6;
        // Pitch the blade
        blade.rotation.y = 0.3;
        fanHub.add(blade);
        meshes.blades.push(blade);
    }

    parts.push({
        name: "Intake Fan System",
        description: "High-powered axial fan that draws air into the tunnel.",
        material: "steel/chrome",
        function: "Provides the necessary air velocity for aerodynamic testing.",
        assemblyOrder: 4,
        connections: ["Test Chamber"],
        failureEffect: "Loss of airflow, rendering tests impossible.",
        cascadeFailures: ["Motor Overheating"],
        originalPosition: { x: -11, y: 0, z: 0 },
        explodedPosition: { x: -15, y: 0, z: 0 }
    });
    meshes.fanHub = fanHub;

    // Flow straightener (Honeycomb)
    const straightenerGeo = new THREE.CylinderGeometry(3.8, 3.8, 0.5, 32);
    const straightener = new THREE.Mesh(straightenerGeo, neonBlue);
    straightener.rotation.z = Math.PI / 2;
    straightener.position.set(-9.5, 0, 0);
    group.add(straightener);

    parts.push({
        name: "Flow Straightener",
        description: "Honeycomb or mesh structure located after the fan.",
        material: "neonBlue",
        function: "Reduces turbulence and swirling in the airflow, ensuring laminar flow over the test subject.",
        assemblyOrder: 5,
        connections: ["Intake Fan System", "Test Chamber"],
        failureEffect: "Turbulent airflow.",
        cascadeFailures: ["Erratic Lift Readings"],
        originalPosition: { x: -9.5, y: 0, z: 0 },
        explodedPosition: { x: -9.5, y: 3, z: 0 }
    });

    // 5. Air Particles (Streamlines)
    const particleCount = 100;
    const particleGeo = new THREE.BoxGeometry(0.6, 0.05, 0.05);
    const particles = new THREE.Group();
    meshes.particles = [];
    
    for(let i=0; i<particleCount; i++) {
        const particle = new THREE.Mesh(particleGeo, airParticleMat);
        // Random initial positions
        particle.position.set(
            (Math.random() - 0.5) * 18,
            (Math.random() - 0.5) * 6,
            (Math.random() - 0.5) * 6
        );
        // Store base Y and Z for streamline path calculation
        particle.userData = {
            baseY: particle.position.y,
            baseZ: particle.position.z,
            speed: 0.1 + Math.random() * 0.05
        };
        particles.add(particle);
        meshes.particles.push(particle);
    }
    group.add(particles);

    parts.push({
        name: "Smoke Streamlines",
        description: "Visualizers injected into the airflow.",
        material: "airParticleMat",
        function: "Allows observation of laminar flow, turbulence, and separation over the airfoil.",
        assemblyOrder: 6,
        connections: ["Flow Straightener"],
        failureEffect: "Invisible airflow.",
        cascadeFailures: [],
        originalPosition: { x: 0, y: 0, z: 0 },
        explodedPosition: { x: 0, y: -8, z: 0 }
    });


    // 6. Angle of Attack Actuator
    const actuatorGeo = new THREE.BoxGeometry(1, 1, 1);
    const actuator = new THREE.Mesh(actuatorGeo, copper);
    actuator.position.set(0, -4, 0);
    group.add(actuator);

    parts.push({
        name: "Angle of Attack Actuator",
        description: "Stepper motor system at the base of the strut.",
        material: "copper",
        function: "Rotates the airfoil to change the angle of attack relative to the airflow.",
        assemblyOrder: 7,
        connections: ["Force Balance Strut"],
        failureEffect: "Stuck at a fixed angle.",
        cascadeFailures: ["Test Incomplete"],
        originalPosition: { x: 0, y: -4, z: 0 },
        explodedPosition: { x: 0, y: -7, z: 0 }
    });


    const description = "The Aerodynamics Airfoil Lift Wind Tunnel is an advanced simulation environment used to analyze the aerodynamic forces on various wing shapes. It features a controlled airflow system with a flow straightener to provide laminar flow, force balance sensors to measure lift and drag, and a variable angle of attack mechanism. Bernoulli's principle and Newton's third law can be observed in action as air accelerates over the curved upper surface, generating lift.";

    const quizQuestions = [
        {
            question: "According to Bernoulli's principle, how does the airfoil generate lift?",
            options: [
                "By pushing air downwards exclusively.",
                "Air travels faster over the curved top, creating lower pressure than the bottom.",
                "Air travels slower over the top, creating higher pressure.",
                "By increasing the density of the air."
            ],
            correct: 1,
            explanation: "Bernoulli's principle states that an increase in fluid speed occurs simultaneously with a decrease in pressure. The airfoil's shape causes air to move faster over the top, resulting in lower pressure on top and higher pressure on the bottom, generating upward lift.",
            difficulty: "Medium"
        },
        {
            question: "What is the primary purpose of the 'Flow Straightener' in the wind tunnel?",
            options: [
                "To cool down the air.",
                "To increase the air pressure.",
                "To remove turbulence and create laminar flow.",
                "To measure the lift force."
            ],
            correct: 2,
            explanation: "The flow straightener (often a honeycomb structure) is used to eliminate swirling and turbulence introduced by the intake fan, ensuring smooth, laminar flow over the test subject for accurate readings.",
            difficulty: "Easy"
        },
        {
            question: "What happens when the 'Angle of Attack' is increased too much?",
            options: [
                "Lift continues to increase infinitely.",
                "The airfoil stalls, causing a sudden loss of lift.",
                "Drag decreases significantly.",
                "The air pressure on the top surface increases."
            ],
            correct: 1,
            explanation: "As the angle of attack increases, lift increases up to a critical point. Beyond this point, the airflow separates from the upper surface, causing a stall, which results in a sudden decrease in lift and an increase in drag.",
            difficulty: "Hard"
        }
    ];

    function animate(time, speed, meshes) {
        // Rotate fan
        if (meshes.fanHub) {
            meshes.fanHub.rotation.x -= 0.5 * speed;
        }

        // Oscillate Airfoil angle of attack slightly
        if (meshes.airfoil) {
            meshes.airfoil.rotation.x = Math.sin(time * 0.5 * speed) * 0.2;
            meshes.strut.rotation.x = meshes.airfoil.rotation.x;
        }

        // Animate particles (Streamlines)
        if (meshes.particles) {
            meshes.particles.forEach(p => {
                p.position.x += p.userData.speed * speed * 10;

                // Airfoil influence (simplified aerodynamics)
                // Airfoil is centered at x=0
                if (p.position.x > -4 && p.position.x < 4) {
                    // Check if particle is near the airfoil vertically
                    if (Math.abs(p.position.y - meshes.airfoil.position.y) < 2) {
                        // Curve up if above, curve down if below
                        if (p.userData.baseY > 0) {
                            p.position.y = p.userData.baseY + Math.sin((p.position.x + 4) * Math.PI / 8) * 1.5;
                        } else {
                            p.position.y = p.userData.baseY - Math.sin((p.position.x + 4) * Math.PI / 8) * 0.5;
                        }
                    }
                } else {
                    // Return to base Y smoothly
                    p.position.y += (p.userData.baseY - p.position.y) * 0.1;
                }

                // Reset position when reaching the end of the chamber
                if (p.position.x > 9) {
                    p.position.x = -9;
                    p.position.y = p.userData.baseY;
                }
            });
        }
    }

    return { group, parts, description, quizQuestions, animate, meshes };
}

