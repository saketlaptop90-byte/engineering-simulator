export function createGlassCockpitPFD(THREE) {
    const machine = new THREE.Group();
    const parts = [];

    // Part 1: LCD screen
    const lcdGeometry = new THREE.BoxGeometry(10, 8, 0.2);
    const lcdMaterial = new THREE.MeshStandardMaterial({ color: 0x111111 });
    const lcd = new THREE.Mesh(lcdGeometry, lcdMaterial);
    lcd.position.set(0, 0, 0.5);
    machine.add(lcd);
    parts.push({ name: 'LCD screen', object: lcd, description: 'Displays flight information including attitude, airspeed, and altitude.' });

    // Part 2: Backlight unit
    const backlightGeometry = new THREE.BoxGeometry(10, 8, 0.1);
    const backlightMaterial = new THREE.MeshStandardMaterial({ color: 0xffffff, emissive: 0x555555 });
    const backlight = new THREE.Mesh(backlightGeometry, backlightMaterial);
    backlight.position.set(0, 0, 0.3);
    machine.add(backlight);
    parts.push({ name: 'Backlight unit', object: backlight, description: 'Provides illumination for the LCD screen.' });

    // Part 3: Graphics processor
    const gpuGeometry = new THREE.BoxGeometry(2, 2, 0.5);
    const gpuMaterial = new THREE.MeshStandardMaterial({ color: 0x00ff00 });
    const gpu = new THREE.Mesh(gpuGeometry, gpuMaterial);
    gpu.position.set(-2, 1, -0.2);
    machine.add(gpu);
    parts.push({ name: 'Graphics processor', object: gpu, description: 'Renders the synthetic vision and flight symbology.' });

    // Part 4: Bezel buttons
    const bezelGeometry = new THREE.BoxGeometry(11, 9, 0.6);
    const bezelMaterial = new THREE.MeshStandardMaterial({ color: 0x222222 });
    const bezel = new THREE.Mesh(bezelGeometry, bezelMaterial);
    bezel.position.set(0, 0, 0.1);
    machine.add(bezel);
    parts.push({ name: 'Bezel buttons', object: bezel, description: 'Allows pilots to interact with the PFD and change settings.' });
    
    // Part 5: Data acquisition unit
    const dataGeometry = new THREE.BoxGeometry(3, 2, 1);
    const dataMaterial = new THREE.MeshStandardMaterial({ color: 0x0000ff });
    const data = new THREE.Mesh(dataGeometry, dataMaterial);
    data.position.set(2, 1, -0.5);
    machine.add(data);
    parts.push({ name: 'Data acquisition unit', object: data, description: 'Collects sensor data from aircraft systems.' });

    // Part 6: Power module
    const powerGeometry = new THREE.BoxGeometry(2, 3, 1);
    const powerMaterial = new THREE.MeshStandardMaterial({ color: 0xff0000 });
    const power = new THREE.Mesh(powerGeometry, powerMaterial);
    power.position.set(-2, -2, -0.5);
    machine.add(power);
    parts.push({ name: 'Power module', object: power, description: 'Regulates and distributes power to the PFD components.' });

    // Part 7: Cooling fan
    const fanGeometry = new THREE.CylinderGeometry(0.5, 0.5, 0.2, 16);
    const fanMaterial = new THREE.MeshStandardMaterial({ color: 0x888888 });
    const fan = new THREE.Mesh(fanGeometry, fanMaterial);
    fan.rotation.x = Math.PI / 2;
    fan.position.set(2, -2, -0.8);
    machine.add(fan);
    parts.push({ name: 'Cooling fan', object: fan, description: 'Dissipates heat generated by the electronics.' });

    // Part 8: Ambient light sensor
    const sensorGeometry = new THREE.SphereGeometry(0.2, 16, 16);
    const sensorMaterial = new THREE.MeshStandardMaterial({ color: 0xaaaaaa });
    const sensor = new THREE.Mesh(sensorGeometry, sensorMaterial);
    sensor.position.set(4.5, 4, 0.4);
    machine.add(sensor);
    parts.push({ name: 'Ambient light sensor', object: sensor, description: 'Measures cockpit lighting to adjust screen brightness automatically.' });

    // Part 9: Anti-glare coating
    const coatingGeometry = new THREE.BoxGeometry(10.1, 8.1, 0.05);
    const coatingMaterial = new THREE.MeshStandardMaterial({ color: 0x333333, transparent: true, opacity: 0.2 });
    const coating = new THREE.Mesh(coatingGeometry, coatingMaterial);
    coating.position.set(0, 0, 0.61);
    machine.add(coating);
    parts.push({ name: 'Anti-glare coating', object: coating, description: 'Reduces reflections to ensure readability in direct sunlight.' });

    // Part 10: System bus interface
    const busGeometry = new THREE.BoxGeometry(8, 0.5, 0.5);
    const busMaterial = new THREE.MeshStandardMaterial({ color: 0xaaaa00 });
    const bus = new THREE.Mesh(busGeometry, busMaterial);
    bus.position.set(0, -3.5, -0.5);
    machine.add(bus);
    parts.push({ name: 'System bus interface', object: bus, description: 'Connects the PFD to the aircraft avionics network (e.g., ARINC 429).' });

    const animate = () => {
        // Animation for the cooling fan and GPU
        fan.rotation.y += 0.2;
        
        // Simulating data processing on GPU by pulsing color
        const time = Date.now() * 0.005;
        gpuMaterial.emissive.setHex(0x00ff00);
        gpuMaterial.emissiveIntensity = 0.5 + 0.5 * Math.sin(time);
    };

    const questions = [
        {
            question: "Which component renders the synthetic vision and flight symbology?",
            options: ["Data acquisition unit", "Graphics processor", "System bus interface", "Power module"],
            correctAnswer: 1
        },
        {
            question: "What is the primary function of the anti-glare coating?",
            options: ["To increase screen brightness", "To reduce reflections and ensure readability", "To protect the screen from scratches", "To process visual data"],
            correctAnswer: 1
        },
        {
            question: "Which part collects sensor data from the aircraft systems?",
            options: ["System bus interface", "Ambient light sensor", "Data acquisition unit", "Cooling fan"],
            correctAnswer: 2
        },
        {
            question: "What does the ambient light sensor do?",
            options: ["Measures altitude", "Adjusts screen brightness automatically", "Detects engine fire", "Provides power to the display"],
            correctAnswer: 1
        },
        {
            question: "Which interface connects the PFD to the avionics network?",
            options: ["System bus interface", "Cooling fan", "Backlight unit", "Bezel buttons"],
            correctAnswer: 0
        },
        {
            question: "Why is a cooling fan necessary in a PFD?",
            options: ["To cool the pilot", "To blow away dust from the screen", "To dissipate heat generated by the electronics", "To provide air pressure for the pitot tube"],
            correctAnswer: 2
        }
    ];

    return {
        group: machine,
        parts: parts,
        animate: animate,
        questions: questions
    };
}
