import { plastic, aluminum, glass, copper, steel, darkSteel, rubber, chrome, tinted } from '../utils/materials.js';

export function createMachine(THREE) {
    const group = new THREE.Group();
    const parts = [];

    // Custom glowing/neon materials
    const laserNeon = new THREE.MeshPhysicalMaterial({
        color: 0x00ff00,
        emissive: 0x00ff00,
        emissiveIntensity: 5,
        transparent: true,
        opacity: 0.8,
        roughness: 0.1,
        metalness: 0.1
    });

    const blueNeon = new THREE.MeshPhysicalMaterial({
        color: 0x00ffff,
        emissive: 0x0088ff,
        emissiveIntensity: 3,
        transparent: true,
        opacity: 0.9,
        roughness: 0.2
    });

    // 1. Base Structure
    const baseGeometry = new THREE.CylinderGeometry(4, 4, 1.5, 32);
    const baseMesh = new THREE.Mesh(baseGeometry, darkSteel);
    baseMesh.position.set(0, 0.75, 0);
    group.add(baseMesh);
    parts.push({
        name: "Main Housing Base",
        description: "Heavy-duty steel base providing stability and vibration isolation for the LIDAR system.",
        material: "Dark Steel",
        function: "Structural support and vibration damping.",
        assemblyOrder: 1,
        connections: ["Power Supply", "Cooling System"],
        failureEffect: "Misalignment of optical paths due to vibration.",
        cascadeFailures: ["Laser Misalignment", "Detector Noise"],
        originalPosition: { x: 0, y: 0.75, z: 0 },
        explodedPosition: { x: 0, y: -2, z: 0 },
        mesh: baseMesh
    });

    // 2. Cooling System / Heatsink
    const heatsinkGeometry = new THREE.BoxGeometry(3, 2, 3);
    const heatsinkMesh = new THREE.Mesh(heatsinkGeometry, aluminum);
    heatsinkMesh.position.set(0, 2.5, 0);
    // Add some fins
    for (let i = -1.2; i <= 1.2; i += 0.4) {
        const finGeo = new THREE.BoxGeometry(3.2, 2, 0.1);
        const fin = new THREE.Mesh(finGeo, aluminum);
        fin.position.set(0, 0, i);
        heatsinkMesh.add(fin);
    }
    group.add(heatsinkMesh);
    parts.push({
        name: "Active Cooling Unit",
        description: "Advanced liquid-cooled heatsink system to maintain optimal operating temperature for the laser.",
        material: "Aluminum",
        function: "Dissipates heat generated by the high-power pulsed laser.",
        assemblyOrder: 2,
        connections: ["Main Housing Base", "Laser Source"],
        failureEffect: "Laser overheating and thermal shutdown.",
        cascadeFailures: ["Laser Degradation", "System Shutdown"],
        originalPosition: { x: 0, y: 2.5, z: 0 },
        explodedPosition: { x: 0, y: 2.5, z: 5 },
        mesh: heatsinkMesh
    });

    // 3. Laser Source
    const laserGeometry = new THREE.CylinderGeometry(0.8, 0.8, 3, 16);
    laserGeometry.rotateX(Math.PI / 2);
    const laserMesh = new THREE.Mesh(laserGeometry, chrome);
    laserMesh.position.set(0, 4.5, 0);
    
    // Laser core ring
    const laserCoreGeo = new THREE.TorusGeometry(0.9, 0.1, 16, 32);
    const laserCoreMesh = new THREE.Mesh(laserCoreGeo, blueNeon);
    laserCoreMesh.rotation.y = Math.PI / 2;
    laserMesh.add(laserCoreMesh);

    group.add(laserMesh);
    parts.push({
        name: "Nd:YAG Pulsed Laser",
        description: "High-power pulsed solid-state laser generating nanosecond light pulses.",
        material: "Chrome / Advanced Alloys",
        function: "Emits intense light pulses into the atmosphere for scattering measurement.",
        assemblyOrder: 3,
        connections: ["Active Cooling Unit", "Beam Expander"],
        failureEffect: "No light emission, system cannot profile atmosphere.",
        cascadeFailures: ["Data Acquisition Failure"],
        originalPosition: { x: 0, y: 4.5, z: 0 },
        explodedPosition: { x: -5, y: 4.5, z: 0 },
        mesh: laserMesh
    });

    // 4. Beam Expander
    const expanderGeometry = new THREE.CylinderGeometry(0.5, 1.2, 2, 32);
    expanderGeometry.rotateX(Math.PI / 2);
    const expanderMesh = new THREE.Mesh(expanderGeometry, steel);
    expanderMesh.position.set(0, 4.5, 2.5);
    group.add(expanderMesh);
    parts.push({
        name: "Beam Expander",
        description: "Optical component that increases the diameter of the laser beam while decreasing its divergence.",
        material: "Steel / Optical Glass",
        function: "Collimate the laser beam to ensure it stays focused over long atmospheric distances.",
        assemblyOrder: 4,
        connections: ["Nd:YAG Pulsed Laser", "Transmitting Mirror"],
        failureEffect: "Beam divergence increases, reducing effective range.",
        cascadeFailures: ["Low Signal-to-Noise Ratio"],
        originalPosition: { x: 0, y: 4.5, z: 2.5 },
        explodedPosition: { x: -5, y: 4.5, z: 5 },
        mesh: expanderMesh
    });

    // 5. Transmitting Mirror
    const mirrorGeometry = new THREE.CylinderGeometry(1.5, 1.5, 0.2, 32);
    mirrorGeometry.rotateX(Math.PI / 4); // Angled at 45 degrees
    const mirrorMesh = new THREE.Mesh(mirrorGeometry, chrome);
    mirrorMesh.position.set(0, 4.5, 4.5);
    group.add(mirrorMesh);
    parts.push({
        name: "Transmitting Mirror",
        description: "Highly reflective dielectric mirror positioned at 45 degrees.",
        material: "Chrome / Dielectric Coating",
        function: "Directs the expanded laser beam upwards into the atmosphere.",
        assemblyOrder: 5,
        connections: ["Beam Expander", "Atmosphere"],
        failureEffect: "Beam reflection angle distorted.",
        cascadeFailures: ["Blind Spots in Profiling"],
        originalPosition: { x: 0, y: 4.5, z: 4.5 },
        explodedPosition: { x: -3, y: 7, z: 5 },
        mesh: mirrorMesh
    });

    // 6. Telescope Mirror (Receiver)
    const telescopeGeometry = new THREE.CylinderGeometry(3.5, 0.5, 4, 32);
    const telescopeMesh = new THREE.Mesh(telescopeGeometry, chrome);
    telescopeMesh.position.set(0, 8, 4.5);
    // make it hollowish
    const lensGeo = new THREE.CylinderGeometry(3.3, 0.4, 4.05, 32);
    const lensMesh = new THREE.Mesh(lensGeo, tinted);
    telescopeMesh.add(lensMesh);
    group.add(telescopeMesh);
    parts.push({
        name: "Cassegrain Telescope Primary",
        description: "Large aperture primary mirror for collecting atmospheric backscatter.",
        material: "Chrome / Glass",
        function: "Collects faint light scattered back from aerosols and molecules in the atmosphere.",
        assemblyOrder: 6,
        connections: ["Main Housing Base", "Detector Assembly"],
        failureEffect: "Loss of receiver sensitivity.",
        cascadeFailures: ["Failure to detect high-altitude aerosols"],
        originalPosition: { x: 0, y: 8, z: 4.5 },
        explodedPosition: { x: 0, y: 12, z: 4.5 },
        mesh: telescopeMesh
    });

    // 7. Detector Assembly (PMT)
    const detectorGeometry = new THREE.CylinderGeometry(0.6, 0.6, 2, 16);
    const detectorMesh = new THREE.Mesh(detectorGeometry, copper);
    detectorMesh.position.set(0, 5, 4.5);
    group.add(detectorMesh);
    parts.push({
        name: "Photomultiplier Tube (PMT)",
        description: "Extremely sensitive light detector capable of single-photon counting.",
        material: "Copper / Electronic Components",
        function: "Converts collected backscattered light into electrical signals.",
        assemblyOrder: 7,
        connections: ["Cassegrain Telescope Primary", "Data Acquisition System"],
        failureEffect: "No signal conversion.",
        cascadeFailures: ["Complete System Data Loss"],
        originalPosition: { x: 0, y: 5, z: 4.5 },
        explodedPosition: { x: 5, y: 5, z: 4.5 },
        mesh: detectorMesh
    });

    // 8. Firing Laser Beam (Visual Effect)
    const beamGeometry = new THREE.CylinderGeometry(0.1, 0.2, 20, 16);
    const beamMesh = new THREE.Mesh(beamGeometry, laserNeon);
    beamMesh.position.set(0, 15, 4.5);
    group.add(beamMesh);
    parts.push({
        name: "Laser Pulse (Visualization)",
        description: "Intense pulse of light travelling through the atmosphere.",
        material: "Neon Green",
        function: "Visual representation of the LIDAR beam.",
        assemblyOrder: 8,
        connections: [],
        failureEffect: "N/A",
        cascadeFailures: [],
        originalPosition: { x: 0, y: 15, z: 4.5 },
        explodedPosition: { x: 0, y: 25, z: 4.5 },
        mesh: beamMesh
    });

    // 9. Data Acquisition System
    const dasGeometry = new THREE.BoxGeometry(2, 4, 2);
    const dasMesh = new THREE.Mesh(dasGeometry, plastic);
    dasMesh.position.set(3, 3, 0);
    // Add some blinking lights to DAS
    const lightGeo = new THREE.SphereGeometry(0.2, 8, 8);
    const lightMesh1 = new THREE.Mesh(lightGeo, laserNeon);
    lightMesh1.position.set(1.1, 1, 0.5);
    const lightMesh2 = new THREE.Mesh(lightGeo, blueNeon);
    lightMesh2.position.set(1.1, 1, -0.5);
    dasMesh.add(lightMesh1);
    dasMesh.add(lightMesh2);

    group.add(dasMesh);
    parts.push({
        name: "Data Acquisition System (DAS)",
        description: "High-speed digitizer and processing unit.",
        material: "Plastic / Silicon",
        function: "Records the time-of-flight of the laser pulses to calculate altitude and scatter intensity.",
        assemblyOrder: 9,
        connections: ["Photomultiplier Tube (PMT)", "Network Interface"],
        failureEffect: "Data corruption or loss.",
        cascadeFailures: ["Software Crash", "Data Pipeline Halt"],
        originalPosition: { x: 3, y: 3, z: 0 },
        explodedPosition: { x: 8, y: 3, z: 0 },
        mesh: dasMesh
    });

    const description = "The Atmospheric LIDAR (Light Detection and Ranging) Profiler is an advanced remote sensing instrument. It fires rapid pulses of laser light into the atmosphere and measures the light scattered back by aerosols, clouds, and gas molecules. By recording the 'time-of-flight' of these reflections, it creates highly accurate vertical profiles of atmospheric properties.";

    const quizQuestions = [
        {
            question: "What is the primary function of the Beam Expander in the LIDAR system?",
            options: [
                "To increase the intensity of the laser",
                "To decrease laser divergence and collimate the beam",
                "To split the beam into multiple wavelengths",
                "To collect backscattered light"
            ],
            correct: 1,
            explanation: "The beam expander increases the initial diameter of the beam, which inversely reduces its divergence over long distances, keeping the beam focused higher in the atmosphere.",
            difficulty: "Medium"
        },
        {
            question: "How does the LIDAR calculate the altitude of atmospheric aerosols?",
            options: [
                "By measuring the angle of the returning light",
                "By measuring the time-of-flight of the laser pulse",
                "By analyzing the Doppler shift of the beam",
                "By using a barometric pressure sensor"
            ],
            correct: 1,
            explanation: "LIDAR uses the 'time-of-flight' principle. By knowing the speed of light, it calculates distance by measuring the time it takes for a laser pulse to travel to the target and reflect back to the detector.",
            difficulty: "Easy"
        },
        {
            question: "Why is a Photomultiplier Tube (PMT) typically used instead of a standard photodiode?",
            options: [
                "It is cheaper to manufacture",
                "It operates at higher temperatures",
                "It has extreme sensitivity, capable of detecting single photons",
                "It generates its own light"
            ],
            correct: 2,
            explanation: "The backscattered light from high altitudes is extremely faint. PMTs offer huge internal gain (multiplication of electrons), allowing them to detect even single returning photons.",
            difficulty: "Hard"
        }
    ];

    function animate(time, speed, activeMeshes) {
        // Animate the laser beam pulsing
        if (activeMeshes.includes(beamMesh)) {
            // Pulse effect using time
            const pulse = (Math.sin(time * 10 * speed) + 1) / 2; // 0 to 1
            laserNeon.opacity = pulse;
            laserNeon.emissiveIntensity = pulse * 10;
            
            // Move beam upwards to simulate pulse traveling
            beamMesh.position.y = 15 + (time * 20 * speed) % 10;
            beamMesh.scale.y = 1 + pulse * 0.5;
        }

        // Animate cooling fins slightly (vibration)
        if (activeMeshes.includes(heatsinkMesh)) {
            heatsinkMesh.position.y = 2.5 + Math.sin(time * 50 * speed) * 0.02;
        }

        // Blinking lights on DAS
        if (activeMeshes.includes(dasMesh)) {
            const blink = Math.floor(time * 5 * speed) % 2 === 0;
            lightMesh1.material.emissiveIntensity = blink ? 5 : 0;
            lightMesh2.material.emissiveIntensity = !blink ? 3 : 0;
        }
    }

    return { group, parts, description, quizQuestions, animate };
}

