export function createPCRThermalCycler(THREE) {
  const group = new THREE.Group();

  // 1. Base/Housing
  const baseGeo = new THREE.BoxGeometry(6, 4, 8);
  const baseMat = new THREE.MeshStandardMaterial({ color: 0xeeeeee, roughness: 0.2 });
  const housing = new THREE.Mesh(baseGeo, baseMat);
  housing.position.y = 2;
  group.add(housing);
  housing.userData = { id: 'housing', name: 'Thermal Cycler Base', description: 'Houses the electronics and Peltier cooling systems.' };

  // 2. Thermal Block
  const blockGeo = new THREE.BoxGeometry(4, 1, 4);
  const blockMat = new THREE.MeshStandardMaterial({ color: 0x888888, metalness: 0.9, roughness: 0.1 });
  const block = new THREE.Mesh(blockGeo, blockMat);
  block.position.set(0, 4.5, 1);
  group.add(block);
  block.userData = { id: 'thermal_block', name: 'Peltier Thermal Block', description: 'Rapidly heats and cools the samples using thermoelectric effects.' };

  // 3. Heated Lid
  const lidPivot = new THREE.Group();
  lidPivot.position.set(0, 5, -1);
  group.add(lidPivot);
  const lidGeo = new THREE.BoxGeometry(4.2, 0.5, 4.2);
  const lidMat = new THREE.MeshStandardMaterial({ color: 0xdddddd });
  const lid = new THREE.Mesh(lidGeo, lidMat);
  lid.position.set(0, 0.25, 2.1);
  lidPivot.add(lid);
  lid.userData = { id: 'heated_lid', name: 'Heated Lid', description: 'Prevents condensation on the top of the PCR tubes during high temperatures.' };

  // 4. PCR Tubes (96-well format, visualized as a few)
  const tubeGeo = new THREE.CylinderGeometry(0.15, 0.05, 0.8, 16);
  const tubeMat = new THREE.MeshStandardMaterial({ color: 0xffffff, transparent: true, opacity: 0.6 });
  const liquidMat = new THREE.MeshBasicMaterial({ color: 0x00aaff });
  const tubesGroup = new THREE.Group();
  
  for(let x=-1.5; x<=1.5; x+=1) {
    for(let z=-0.5; z<=2.5; z+=1) {
      const tube = new THREE.Mesh(tubeGeo, tubeMat);
      tube.position.set(x, 4.5, z);
      tubesGroup.add(tube);
      
      const liquid = new THREE.Mesh(new THREE.CylinderGeometry(0.1, 0.06, 0.4), liquidMat);
      liquid.position.set(x, 4.3, z);
      tubesGroup.add(liquid);
      
      if(x===0 && z===0.5) tube.userData = { id: 'pcr_tubes', name: 'Microcentrifuge Tubes', description: 'Contains the PCR master mix: Taq polymerase, primers, dNTPs, and template DNA.' };
    }
  }
  group.add(tubesGroup);

  // 5. Touchscreen UI
  const screenGeo = new THREE.PlaneGeometry(3, 2);
  const screenMat = new THREE.MeshBasicMaterial({ color: 0x000000 });
  const screen = new THREE.Mesh(screenGeo, screenMat);
  screen.position.set(0, 2.5, 4.01);
  group.add(screen);
  screen.userData = { id: 'screen', name: 'Touchscreen Interface', description: 'Programs the Denaturation, Annealing, and Extension temperature cycles.' };

  // 6. Denaturation Graph Line (94°C)
  const lineGeo = new THREE.PlaneGeometry(0.8, 0.05);
  const lineMat = new THREE.MeshBasicMaterial({ color: 0xff0000 });
  const denat = new THREE.Mesh(lineGeo, lineMat);
  denat.position.set(-1, 3, 4.02);
  group.add(denat);
  denat.userData = { id: 'denaturation', name: 'Denaturation (94-98°C)', description: 'Heat breaks hydrogen bonds, separating the DNA double helix.' };

  // 7. Annealing Graph Line (55°C)
  const annGeo = new THREE.PlaneGeometry(0.8, 0.05);
  const annMat = new THREE.MeshBasicMaterial({ color: 0x00ff00 });
  const anneal = new THREE.Mesh(annGeo, annMat);
  anneal.position.set(0, 2.2, 4.02);
  group.add(anneal);
  anneal.userData = { id: 'annealing', name: 'Annealing (50-65°C)', description: 'Cooling allows primers to bind to the specific target DNA sequences.' };

  // 8. Extension Graph Line (72°C)
  const extGeo = new THREE.PlaneGeometry(0.8, 0.05);
  const extMat = new THREE.MeshBasicMaterial({ color: 0x0000ff });
  const extend = new THREE.Mesh(extGeo, extMat);
  extend.position.set(1, 2.6, 4.02);
  group.add(extend);
  extend.userData = { id: 'extension', name: 'Extension (72°C)', description: 'Taq polymerase synthesizes a new DNA strand.' };

  // 9. Heat Sink (Back)
  const sinkGeo = new THREE.BoxGeometry(4, 3, 1);
  const sinkMat = new THREE.MeshStandardMaterial({ color: 0x333333 });
  const sink = new THREE.Mesh(sinkGeo, sinkMat);
  sink.position.set(0, 2, -3.5);
  group.add(sink);
  sink.userData = { id: 'heat_sink', name: 'Aluminum Heat Sink', description: 'Dissipates massive amounts of heat generated by the Peltier elements.' };

  // 10. Cooling Fan
  const fanGeo = new THREE.CylinderGeometry(1.5, 1.5, 0.2, 16);
  const fanMat = new THREE.MeshStandardMaterial({ color: 0x111111 });
  const fan = new THREE.Mesh(fanGeo, fanMat);
  fan.position.set(0, 2, -4);
  fan.rotation.x = Math.PI / 2;
  group.add(fan);
  fan.userData = { id: 'cooling_fan', name: 'High-RPM Fan', description: 'Crucial for achieving cooling rates of 3-5°C per second.' };

  group.userData.animate = function(delta) {
    // Open/Close lid slowly
    lidPivot.rotation.x = -Math.PI / 4 + Math.sin(Date.now() * 0.001) * (Math.PI / 4);
    
    // Spin fan
    fan.rotation.y += 0.5;
    
    // Cycle block color (Red = hot, Blue = cold)
    const t = Date.now() * 0.002;
    const cycle = Math.sin(t);
    if(cycle > 0.5) block.material.color.setHex(0xffaaaa); // Denature
    else if(cycle < -0.5) block.material.color.setHex(0xaaaaff); // Anneal
    else block.material.color.setHex(0xaaffaa); // Extend
  };

  group.userData.quiz = [
    { question: "Why is Taq polymerase used in PCR instead of human DNA polymerase?", options: ["It works faster", "It can survive the 95°C denaturation step without being destroyed", "It is cheaper to produce"], answer: 1 },
    { question: "If you start with 1 DNA copy, how many do you have after 30 PCR cycles?", options: ["30", "900", "Over 1 billion (2^30)"], answer: 2 }
  ];

  return group;
}
