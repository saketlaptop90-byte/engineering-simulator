import * as THREE from 'three';
export function createBerylliumHiggsField() {
  const group = new THREE.Group();
  
  // The Higgs Field giving mass to the electrons and quarks
  
  // A dense grid of glowing particles (The Field)
  const field = new THREE.Group();
  for(let x=-2; x<=2; x+=0.5) {
      for(let z=-2; z<=2; z+=0.5) {
          const p = new THREE.Mesh(new THREE.SphereGeometry(0.05, 8, 8), new THREE.MeshBasicMaterial({color: 0x00ff00, transparent: true, opacity: 0.3}));
          p.position.set(x, 0, z);
          field.add(p);
      }
  }
  group.add(field);

  // Beryllium atom pushing through the field
  const atom = new THREE.Mesh(new THREE.SphereGeometry(0.4, 16, 16), new THREE.MeshBasicMaterial({color: 0x00ffff}));
  atom.position.set(-3, 0, 0);
  group.add(atom);

  // Bow shock (Interaction with the field giving it mass)
  const shock = new THREE.Mesh(new THREE.ConeGeometry(1, 2, 16), new THREE.MeshBasicMaterial({color: 0x00ff00, transparent: true, opacity: 0.4, wireframe: true}));
  shock.rotation.z = -Math.PI/2;
  shock.position.set(-3.5, 0, 0);
  group.add(shock);

  group.userData.animate = function(delta, time, speed) {
      // Pan camera slightly
      group.rotation.y = Math.sin(time*speed*0.1)*0.2;
      
      const cycle = (time * speed * 0.5) % 3;
      
      // Drag atom through the field
      const pos = -2 + cycle*2;
      atom.position.x = pos;
      shock.position.x = pos - 0.5;
      
      // Disturb the field particles as it passes
      field.children.forEach(p => {
          const dist = Math.abs(p.position.x - atom.position.x);
          if (dist < 1.0) {
              p.position.y = Math.sin(dist * Math.PI) * 0.5;
              p.material.opacity = 1;
          } else {
              p.position.y = 0;
              p.material.opacity = 0.3;
          }
      });
  };

  return {
    group: group,
    description: "The Higgs Field. Why does Beryllium have 'mass' (weight)? Mass is not an inherent property of objects. The entire universe is filled with an invisible syrup called the 'Higgs Field'. When the electrons and quarks inside Beryllium try to move through space, they 'drag' against this field like a person walking through water. This resistance to movement IS what we call 'Mass' or 'Inertia'. If the Higgs field turned off, the Beryllium atom would become massless and instantly disintegrate at the speed of light.",
    parts: [
      { name: "Green Grid", material: "The Higgs Field", function: "An omnipresent quantum field permeating all of space." },
      { name: "Cyan Sphere", material: "Beryllium Atom", function: "Dragging through the field." },
      { name: "Green Shockwave", material: "Mass (Inertia)", function: "The resistance generated by interacting with the field." }
    ],
    quizQuestions: [
      { question: "According to the Standard Model of particle physics, where does an atom get its mass (weight)?", options: ["From gravity", "From interacting with the omnipresent Higgs Field, which creates 'drag' or inertia", "From its size", "From the speed of light"], correct: 1, explanation: "Without the Higgs field, all fundamental particles would be massless and zip around at the speed of light. The Higgs field gives them 'drag', slowing them down and allowing them to clump into atoms." }
    ]
  };
}