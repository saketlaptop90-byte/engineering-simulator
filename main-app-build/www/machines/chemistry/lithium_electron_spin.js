import * as THREE from 'three';
export function createLithiumElectronSpin() {
  const group = new THREE.Group();
  
  // Electron Spin Quantum Number (ms) (Remastered)
  
  const createElectron = (color, spinDir, xPos) => {
      const g = new THREE.Group();
      
      // The electron body
      const e = new THREE.Mesh(
          new THREE.SphereGeometry(1, 32, 32),
          new THREE.MeshPhysicalMaterial({color: color, emissive: 0x222222, metalness: 0.5})
      );
      g.add(e);
      
      // Magnetic field lines (Torus rings rotating around it)
      const field = new THREE.Group();
      for(let i=0; i<3; i++) {
          const ring = new THREE.Mesh(
              new THREE.TorusGeometry(1.5 + i*0.5, 0.05, 16, 64),
              new THREE.MeshBasicMaterial({color: 0xffffff, transparent: true, opacity: 0.3})
          );
          ring.rotation.x = Math.PI/2;
          field.add(ring);
      }
      g.add(field);
      
      // Spin Vector Arrow (North/South pole)
      const arrowLen = 3;
      const arrow = new THREE.Group();
      const stick = new THREE.Mesh(new THREE.CylinderGeometry(0.1, 0.1, arrowLen), new THREE.MeshBasicMaterial({color: 0xff0000}));
      const head = new THREE.Mesh(new THREE.ConeGeometry(0.4, 1, 16), new THREE.MeshBasicMaterial({color: 0xff0000}));
      head.position.y = arrowLen/2;
      arrow.add(stick, head);
      
      if (spinDir < 0) {
          arrow.rotation.x = Math.PI; // Spin Down
          arrow.children[0].material.color.setHex(0x0000ff);
          arrow.children[1].material.color.setHex(0x0000ff);
      }
      g.add(arrow);
      
      g.position.x = xPos;
      g.userData = { spinDir: spinDir, eMesh: e, field: field };
      return g;
  };
  
  const spinUp = createElectron(0x00ffff, 1, -3);
  const spinDown = createElectron(0xff00ff, -1, 3);
  
  group.add(spinUp, spinDown);

  const light = new THREE.DirectionalLight(0xffffff, 2);
  light.position.set(0, 5, 5);
  group.add(light, new THREE.AmbientLight(0x404040, 2));

  group.userData.animate = function(delta, time, speed) {
      group.rotation.x = Math.sin(time*speed*0.1)*0.2 + 0.2;
      group.rotation.y = Math.sin(time*speed*0.1)*0.2;
      
      // Rotate the electrons based on their spin
      spinUp.userData.eMesh.rotation.y += delta * speed * 5;
      spinUp.userData.field.rotation.y -= delta * speed * 2; // field moves
      
      spinDown.userData.eMesh.rotation.y -= delta * speed * 5;
      spinDown.userData.field.rotation.y += delta * speed * 2;
  };

  return {
    group: group,
    description: "Electron Spin Quantum Number (Remastered). The final quantum number is 'Spin' (ms). Electrons aren't actually physically spinning like tops, but they possess an intrinsic quantum property that makes them act exactly like tiny bar magnets! Because of quantum mechanics, an electron can only 'spin' in two possible directions: Spin Up (+1/2, red arrow) or Spin Down (-1/2, blue arrow). Because they act like magnets, two electrons with opposite spins will magnetically attract each other, which helps counteract the fact that their negative charges are trying to push them apart!",
    parts: [
      { name: "Cyan Sphere (Red Arrow)", material: "Spin Up (+1/2)", function: "Magnetic north pole pointing 'up'." },
      { name: "Magenta Sphere (Blue Arrow)", material: "Spin Down (-1/2)", function: "Magnetic north pole pointing 'down'." },
      { name: "White Rings", material: "Magnetic Field", function: "Generated by the quantum spin." }
    ],
    quizQuestions: [
      { question: "Why do two electrons in the same orbital need to have opposite spins?", options: ["So they look pretty", "Because their opposite magnetic fields (up and down) create an attraction that helps overcome their electrostatic repulsion.", "Because one is positive", "Because they are heavy"], correct: 1, explanation: "If you try to shove two magnets together with the same poles facing, they repel! Electrons must have opposite spins to coexist in the same space." }
    ]
  };
}