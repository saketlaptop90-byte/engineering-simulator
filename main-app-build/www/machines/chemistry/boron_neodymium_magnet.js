import * as THREE from 'three';
export function createBoronNeodymiumMagnet() {
  const group = new THREE.Group();
  
  // Nd2Fe14B (Neodymium Magnet Unit Cell)
  
  const ndMat = new THREE.MeshPhysicalMaterial({color: 0xaa00ff, metalness: 0.9, roughness: 0.1}); // Neodymium (Huge, Purple)
  const feMat = new THREE.MeshPhysicalMaterial({color: 0x888888, metalness: 1.0, roughness: 0.3}); // Iron (Silver)
  const bMat = new THREE.MeshPhysicalMaterial({color: 0x00aaff, metalness: 0.5, roughness: 0.2}); // Boron (Cyan)
  
  // A stylized representation of the incredibly complex tetragonal unit cell
  // Instead of all 68 atoms, we build a beautiful representative layered lattice
  
  const createLayer = (y, isNdLayer) => {
      const layer = new THREE.Group();
      if (isNdLayer) {
          // Nd and B layer
          const nd1 = new THREE.Mesh(new THREE.SphereGeometry(0.7, 32,32), ndMat); nd1.position.set(-2, 0, -2); layer.add(nd1);
          const nd2 = new THREE.Mesh(new THREE.SphereGeometry(0.7, 32,32), ndMat); nd2.position.set(2, 0, 2); layer.add(nd2);
          
          const b1 = new THREE.Mesh(new THREE.SphereGeometry(0.3, 32,32), bMat); b1.position.set(0, 0, 0); layer.add(b1);
      } else {
          // Iron heavy layer
          for(let x=-2; x<=2; x+=2) {
              for(let z=-2; z<=2; z+=2) {
                  if (x===0 && z===0) continue; // leave hole
                  const fe = new THREE.Mesh(new THREE.SphereGeometry(0.4, 32,32), feMat);
                  fe.position.set(x, 0, z);
                  layer.add(fe);
              }
          }
      }
      layer.position.y = y;
      group.add(layer);
  };
  
  createLayer(-3, false); // Fe
  createLayer(-1.5, true);  // Nd/B
  createLayer(0, false);  // Fe
  createLayer(1.5, true);   // Nd/B
  createLayer(3, false);  // Fe
  
  // Magnetic Flux Lines (Glowing wires passing straight up through the crystal)
  const fluxGrp = new THREE.Group();
  const fluxMat = new THREE.MeshBasicMaterial({color: 0x00ffff, transparent: true, opacity: 0.5, blending: THREE.AdditiveBlending});
  for(let i=0; i<10; i++) {
      const line = new THREE.Mesh(new THREE.CylinderGeometry(0.05, 0.05, 12, 8), fluxMat);
      line.position.set((Math.random()-0.5)*5, 0, (Math.random()-0.5)*5);
      fluxGrp.add(line);
  }
  group.add(fluxGrp);

  const light = new THREE.DirectionalLight(0xffffff, 2);
  light.position.set(5,5,5);
  group.add(light, new THREE.AmbientLight(0x404040, 2));

  group.userData.animate = function(delta, time, speed) {
      group.rotation.y = time * speed * 0.1;
      
      // Animate magnetic flux moving upward
      fluxGrp.children.forEach((line, idx) => {
          line.position.y += delta * speed * 5.0;
          if (line.position.y > 6) {
              line.position.y = -6;
          }
      });
  };

  return {
    group: group,
    description: "Neodymium Magnet Lattice (Nd2Fe14B). The strongest permanent magnets in the world! They are used in electric car motors, wind turbines, and hard drives. While Neodymium and Iron provide the magnetic force, they can't form a stable crystal together. Boron (the small cyan atom) acts as the critical 'glue', sitting inside the lattice and locking the massive Iron and Neodymium atoms into a rigid, highly directional crystal structure. Without Boron, the crystal would collapse and the magnetic fields would scramble!",
    parts: [
      { name: "Large Purple Spheres", material: "Neodymium (Nd)", function: "Provides the incredibly strong magnetic field." },
      { name: "Silver Spheres", material: "Iron (Fe)", function: "Connects the magnetic domains." },
      { name: "Small Cyan Spheres", material: "Boron (B)", function: "The structural 'glue' that forces the crystal to stay perfectly aligned." },
      { name: "Glowing Cyan Lines", material: "Magnetic Flux", function: "The unidirectional magnetic field generated by the locked crystal." }
    ],
    quizQuestions: [
      { question: "In a Neodymium Magnet (Nd2Fe14B), what is the specific job of the Boron atoms?", options: ["To make it magnetic", "To act as the structural 'glue' that locks the Neodymium and Iron atoms into a rigid crystal, preventing the magnetic fields from scrambling.", "To make it heavier", "To change its color"], correct: 1, explanation: "Boron isn't magnetic itself! Its job is purely structural. It wedges itself into the gaps between the giant metal atoms, locking them in place so their magnetic spins all point in the exact same direction." }
    ]
  };
}