export function createTransmissionElectronMicroscope(THREE) {
    const group = new THREE.Group();

    // Materials
    const gunMat = new THREE.MeshStandardMaterial({ color: 0xdddddd, metalness: 0.8, roughness: 0.2 });
    const anodeMat = new THREE.MeshStandardMaterial({ color: 0xffd700, metalness: 0.9, roughness: 0.1 });
    const lensMat1 = new THREE.MeshStandardMaterial({ color: 0x1f3b4d, metalness: 0.7, roughness: 0.3 });
    const lensMat2 = new THREE.MeshStandardMaterial({ color: 0x2a4d69, metalness: 0.7, roughness: 0.3 });
    const lensMat3 = new THREE.MeshStandardMaterial({ color: 0x4b86b4, metalness: 0.7, roughness: 0.3 });
    const holderMat = new THREE.MeshStandardMaterial({ color: 0xb87333, metalness: 0.8, roughness: 0.2 });
    const screenMat = new THREE.MeshStandardMaterial({ color: 0x88ff88, emissive: 0x004400, metalness: 0.1, roughness: 0.8 });
    const sensorMat = new THREE.MeshStandardMaterial({ color: 0x222222, metalness: 0.5, roughness: 0.5 });
    const beamMat = new THREE.MeshStandardMaterial({ color: 0x00ffff, emissive: 0x00ffff, emissiveIntensity: 1.0, transparent: true, opacity: 0.6 });

    // 1. Electron Gun
    const gunGeo = new THREE.CylinderGeometry(1.5, 1.5, 2, 32);
    const gun = new THREE.Mesh(gunGeo, gunMat);
    gun.position.set(0, 7, 0);
    gun.name = "Electron Gun";
    gun.userData = { 
        id: 'electron_gun', 
        name: 'Electron Gun', 
        description: 'Generates a high-velocity stream of electrons via thermionic or field emission.' 
    };
    group.add(gun);

    // 2. Anode Plate
    const anodeGeo = new THREE.TorusGeometry(1, 0.3, 16, 32);
    const anode = new THREE.Mesh(anodeGeo, anodeMat);
    anode.rotation.x = Math.PI / 2;
    anode.position.set(0, 5, 0);
    anode.name = "Anode Plate";
    anode.userData = { 
        id: 'anode_plate', 
        name: 'Anode Plate', 
        description: 'Accelerates the electrons generated by the gun down the column using a high positive potential.' 
    };
    group.add(anode);

    // 3. Condenser Lens
    const condGeo = new THREE.CylinderGeometry(2, 2, 1.5, 32);
    const condenser = new THREE.Mesh(condGeo, lensMat1);
    condenser.position.set(0, 3.5, 0);
    condenser.name = "Electromagnetic Condenser Lens";
    condenser.userData = { 
        id: 'condenser_lens', 
        name: 'Electromagnetic Condenser Lens', 
        description: 'Focuses the electron beam onto the specimen, controlling beam intensity and illumination area.' 
    };
    group.add(condenser);

    // 4. Specimen Holder
    const holderGeo = new THREE.CylinderGeometry(0.2, 0.2, 5, 16);
    const holder = new THREE.Mesh(holderGeo, holderMat);
    holder.rotation.z = Math.PI / 2;
    holder.position.set(2.5, 2, 0);
    holder.name = "Specimen Holder";
    holder.userData = { 
        id: 'specimen_holder', 
        name: 'Specimen Holder', 
        description: 'Secures the ultra-thin sample in the beam path, allowing fine manipulation and tilting.' 
    };
    group.add(holder);

    // 5. Objective Lens
    const objGeo = new THREE.CylinderGeometry(2.5, 2.5, 1.5, 32);
    const objective = new THREE.Mesh(objGeo, lensMat2);
    objective.position.set(0, 0.5, 0);
    objective.name = "Objective Lens";
    objective.userData = { 
        id: 'objective_lens', 
        name: 'Objective Lens', 
        description: 'The most critical lens; it forms the first intermediate image of the specimen with high resolution.' 
    };
    group.add(objective);

    // 6. Intermediate Lens
    const intGeo = new THREE.CylinderGeometry(1.8, 1.8, 1.2, 32);
    const intermediate = new THREE.Mesh(intGeo, lensMat3);
    intermediate.position.set(0, -1.5, 0);
    intermediate.name = "Intermediate Lens";
    intermediate.userData = { 
        id: 'intermediate_lens', 
        name: 'Intermediate Lens', 
        description: 'Magnifies the image or diffraction pattern formed by the objective lens.' 
    };
    group.add(intermediate);

    // 7. Projector Lens
    const projGeo = new THREE.CylinderGeometry(2, 3, 1.5, 32);
    const projector = new THREE.Mesh(projGeo, lensMat1);
    projector.position.set(0, -3.5, 0);
    projector.name = "Projector Lens";
    projector.userData = { 
        id: 'projector_lens', 
        name: 'Projector Lens', 
        description: 'Further magnifies the image and projects it onto the fluorescent screen or camera sensor.' 
    };
    group.add(projector);

    // 8. Fluorescent Screen
    const screenGeo = new THREE.CylinderGeometry(4, 4, 0.2, 32);
    const screen = new THREE.Mesh(screenGeo, screenMat);
    screen.position.set(0, -5.5, 0);
    screen.name = "Fluorescent Screen";
    screen.userData = { 
        id: 'fluorescent_screen', 
        name: 'Fluorescent Screen', 
        description: 'Coated with phosphors that emit light when struck by electrons, allowing direct viewing of the image.' 
    };
    group.add(screen);

    // 9. Camera Sensor
    const sensorGeo = new THREE.BoxGeometry(2, 1, 2);
    const sensor = new THREE.Mesh(sensorGeo, sensorMat);
    sensor.position.set(0, -7, 0);
    sensor.name = "Camera Sensor";
    sensor.userData = { 
        id: 'camera_sensor', 
        name: 'Camera Sensor', 
        description: 'Captures the final high-resolution digital image of the transmitted electron pattern.' 
    };
    group.add(sensor);

    // 10. Electron Beam
    const beamGeo = new THREE.CylinderGeometry(0.15, 0.15, 12.5, 16);
    const beam = new THREE.Mesh(beamGeo, beamMat);
    beam.position.set(0, 0.75, 0);
    beam.name = "Electron Beam";
    beam.userData = { 
        id: 'electron_beam', 
        name: 'Electron Beam', 
        description: 'A stream of accelerated electrons that interacts with the specimen to form the magnified image.' 
    };
    group.add(beam);

    // Add tick function for animation
    group.tick = (time) => {
        // Animate the electron beam opacity and scale to simulate pulsing
        beam.material.opacity = 0.5 + 0.3 * Math.sin(time * 8);
        beam.scale.x = 1 + 0.15 * Math.sin(time * 15);
        beam.scale.z = 1 + 0.15 * Math.sin(time * 15);

        // Animate the fluorescent screen glowing intensity
        screen.material.emissiveIntensity = 0.5 + 0.5 * Math.sin(time * 8);

        // Slowly tilt specimen holder to simulate alignment
        holder.rotation.x = 0.1 * Math.sin(time);
    };

    // Quiz Questions
    group.userData.quiz = [
        {
            question: "Which component is responsible for initially generating the stream of electrons?",
            options: [
                "Electron Gun",
                "Objective Lens",
                "Specimen Holder",
                "Fluorescent Screen"
            ],
            correctAnswer: 0
        },
        {
            question: "What is the primary function of the Electromagnetic Condenser Lens?",
            options: [
                "To magnify the final image",
                "To convert electrons into visible light",
                "To focus the electron beam onto the specimen",
                "To accelerate the electrons"
            ],
            correctAnswer: 2
        },
        {
            question: "Which lens forms the first, most critical intermediate image of the specimen?",
            options: [
                "Objective Lens",
                "Projector Lens",
                "Condenser Lens",
                "Intermediate Lens"
            ],
            correctAnswer: 0
        },
        {
            question: "How is the final image typically viewed directly by the operator?",
            options: [
                "Inside the Electron Gun",
                "Through the Anode Plate",
                "Via the Specimen Holder",
                "On the Fluorescent Screen"
            ],
            correctAnswer: 3
        },
        {
            question: "What role does the Anode Plate play in a TEM?",
            options: [
                "It accelerates the electrons down the column",
                "It holds the specimen in place",
                "It captures digital photographs",
                "It bends the electrons to form a diffraction pattern"
            ],
            correctAnswer: 0
        },
        {
            question: "Why must the specimen be ultra-thin in a Transmission Electron Microscope?",
            options: [
                "To prevent it from melting in the vacuum",
                "So electrons can pass through it to form an image",
                "To make it easier for the camera sensor to process",
                "To increase the focal length of the projector lens"
            ],
            correctAnswer: 1
        }
    ];

    return group;
}
