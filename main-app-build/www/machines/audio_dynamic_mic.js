import { plastic, aluminum, glass, copper, steel, darkSteel, rubber, chrome, tinted } from '../utils/materials.js';

export function createMachine(THREE) {
    const group = new THREE.Group();
    const parts = [];

    // Custom glowing materials
    const glowBlue = new THREE.MeshPhysicalMaterial({
        color: 0x00aaff,
        emissive: 0x0088ff,
        emissiveIntensity: 1.5,
        transparent: true,
        opacity: 0.8,
        metalness: 0.1,
        roughness: 0.2
    });

    const glowRed = new THREE.MeshPhysicalMaterial({
        color: 0xff3300,
        emissive: 0xff1100,
        emissiveIntensity: 1.2,
        transparent: true,
        opacity: 0.7
    });

    const glowPurple = new THREE.MeshPhysicalMaterial({
        color: 0xaa00ff,
        emissive: 0x8800ff,
        emissiveIntensity: 1.8,
        wireframe: true
    });

    // Parts

    // 1. Microphone Body (Handle)
    const handleGeo = new THREE.CylinderGeometry(0.8, 0.5, 4, 32);
    const handle = new THREE.Mesh(handleGeo, darkSteel);
    handle.position.set(0, -2, 0);
    group.add(handle);
    parts.push({
        name: "Mic Body",
        mesh: handle,
        description: "The rugged outer casing of the microphone, providing shielding and a grip.",
        material: "Die-cast Metal",
        function: "Houses internal components and protects them from physical damage and electromagnetic interference.",
        assemblyOrder: 1,
        connections: ["XLR Connector", "Magnet Assembly"],
        failureEffect: "Structural vulnerability, increased susceptibility to RF interference.",
        cascadeFailures: ["Signal degradation from EMI"],
        originalPosition: { x: 0, y: -2, z: 0 },
        explodedPosition: { x: 0, y: -4, z: 0 }
    });

    // 2. XLR Connector (Bottom)
    const xlrGeo = new THREE.CylinderGeometry(0.45, 0.45, 0.5, 32);
    const xlr = new THREE.Mesh(xlrGeo, chrome);
    xlr.position.set(0, -4.25, 0);
    group.add(xlr);
    
    // 3 XLR Pins
    const pinGeo = new THREE.CylinderGeometry(0.05, 0.05, 0.3, 16);
    const pin1 = new THREE.Mesh(pinGeo, copper);
    pin1.position.set(0, -4.4, 0.2);
    group.add(pin1);
    const pin2 = new THREE.Mesh(pinGeo, copper);
    pin2.position.set(0.17, -4.4, -0.1);
    group.add(pin2);
    const pin3 = new THREE.Mesh(pinGeo, copper);
    pin3.position.set(-0.17, -4.4, -0.1);
    group.add(pin3);

    parts.push({
        name: "XLR Connector",
        mesh: xlr,
        description: "Standard 3-pin audio connector.",
        material: "Chrome/Copper Pins",
        function: "Outputs the balanced audio signal generated by the microphone.",
        assemblyOrder: 2,
        connections: ["Mic Body", "Moving Coil"],
        failureEffect: "Loss of audio signal or introduction of severe ground hum.",
        cascadeFailures: ["Complete system failure"],
        originalPosition: { x: 0, y: -4.25, z: 0 },
        explodedPosition: { x: 0, y: -6, z: 0 }
    });

    // 3. Magnet Assembly
    const magnetGeo = new THREE.CylinderGeometry(0.7, 0.7, 1.5, 32);
    const magnet = new THREE.Mesh(magnetGeo, steel);
    magnet.position.set(0, 0.75, 0);
    group.add(magnet);
    
    // Inner Magnet Core (Neodymium)
    const coreGeo = new THREE.CylinderGeometry(0.3, 0.3, 1.6, 32);
    const core = new THREE.Mesh(coreGeo, glowBlue);
    core.position.set(0, 0.75, 0);
    group.add(core);

    parts.push({
        name: "Neodymium Magnet Assembly",
        mesh: magnet,
        description: "Provides a strong, permanent magnetic field.",
        material: "Neodymium / Steel",
        function: "Creates the stationary magnetic field within which the voice coil is suspended.",
        assemblyOrder: 3,
        connections: ["Mic Body", "Moving Coil"],
        failureEffect: "Loss of sensitivity and signal output.",
        cascadeFailures: ["Signal-to-noise ratio drop"],
        originalPosition: { x: 0, y: 0.75, z: 0 },
        explodedPosition: { x: 0, y: 0.75, z: 2 }
    });

    // 4. Voice Coil (Moving Coil)
    const coilGeo = new THREE.CylinderGeometry(0.35, 0.35, 0.8, 32, 1, true);
    const coil = new THREE.Mesh(coilGeo, copper);
    coil.position.set(0, 1.3, 0);
    group.add(coil);
    
    const coilGlowGeo = new THREE.CylinderGeometry(0.36, 0.36, 0.8, 32, 1, true);
    const coilGlow = new THREE.Mesh(coilGlowGeo, glowRed);
    coilGlow.position.set(0, 1.3, 0);
    group.add(coilGlow);

    parts.push({
        name: "Voice Coil",
        mesh: coil,
        description: "A fine wire coil suspended within the magnetic gap.",
        material: "Copper Wire",
        function: "Moves with the diaphragm to induce an electrical current via electromagnetic induction.",
        assemblyOrder: 4,
        connections: ["Diaphragm", "Magnet Assembly"],
        failureEffect: "No electrical signal generated.",
        cascadeFailures: ["Complete audio loss"],
        originalPosition: { x: 0, y: 1.3, z: 0 },
        explodedPosition: { x: 0, y: 2.5, z: 0 }
    });

    // 5. Diaphragm
    const diaphragmGeo = new THREE.SphereGeometry(0.7, 32, 16, 0, Math.PI * 2, 0, Math.PI / 4);
    const diaphragm = new THREE.Mesh(diaphragmGeo, plastic);
    diaphragm.position.set(0, 1.1, 0);
    group.add(diaphragm);
    
    const diaphragmGlowGeo = new THREE.SphereGeometry(0.71, 32, 16, 0, Math.PI * 2, 0, Math.PI / 4);
    const diaphragmGlow = new THREE.Mesh(diaphragmGlowGeo, glowPurple);
    diaphragmGlow.position.set(0, 1.1, 0);
    group.add(diaphragmGlow);

    parts.push({
        name: "Diaphragm",
        mesh: diaphragm,
        description: "A thin, flexible membrane usually made of Mylar.",
        material: "Mylar (Plastic)",
        function: "Vibrates in response to sound waves, driving the movement of the voice coil.",
        assemblyOrder: 5,
        connections: ["Voice Coil", "Grille"],
        failureEffect: "Distorted sound or lack of high-frequency response.",
        cascadeFailures: ["Voice coil misalignment"],
        originalPosition: { x: 0, y: 1.1, z: 0 },
        explodedPosition: { x: 0, y: 4, z: 0 }
    });

    // 6. Grille (Mesh Cap)
    const grilleGeo = new THREE.SphereGeometry(1.2, 32, 16, 0, Math.PI * 2, 0, Math.PI / 2);
    const grilleMaterial = new THREE.MeshPhysicalMaterial({
        color: 0x888888,
        metalness: 0.8,
        roughness: 0.2,
        wireframe: true,
        transparent: true,
        opacity: 0.5
    });
    const grille = new THREE.Mesh(grilleGeo, grilleMaterial);
    grille.position.set(0, 1.5, 0);
    group.add(grille);

    parts.push({
        name: "Wire Mesh Grille",
        mesh: grille,
        description: "Spherical metal mesh covering the capsule.",
        material: "Steel",
        function: "Protects the delicate diaphragm and acts as a pop filter to reduce plosives and wind noise.",
        assemblyOrder: 6,
        connections: ["Mic Body"],
        failureEffect: "Exposes capsule to physical damage and breath plosives.",
        cascadeFailures: ["Diaphragm puncture"],
        originalPosition: { x: 0, y: 1.5, z: 0 },
        explodedPosition: { x: 0, y: 6, z: 0 }
    });

    const description = "A high-tech Audio Dynamic Microphone visualizing electromagnetic induction. Sound waves vibrate the diaphragm, moving the attached voice coil through a permanent magnetic field. This motion induces an alternating current analog of the sound wave. The visualization highlights the magnetic core in blue, the vibrating coil in red, and the acoustic pressure waves hitting the diaphragm in purple.";

    const quizQuestions = [
        {
            question: "What physical principle allows a dynamic microphone to convert sound into an electrical signal?",
            options: ["Piezoelectric effect", "Electromagnetic induction", "Capacitance change", "Photoelectric effect"],
            correct: 1,
            explanation: "Dynamic microphones work via electromagnetic induction: a moving coil within a magnetic field induces an electrical current.",
            difficulty: "Medium"
        },
        {
            question: "Which component directly vibrates in response to acoustic sound waves?",
            options: ["Magnet", "XLR Connector", "Diaphragm", "Handle"],
            correct: 2,
            explanation: "The diaphragm is a thin membrane that catches sound waves and vibrates, driving the voice coil.",
            difficulty: "Easy"
        },
        {
            question: "What is the primary function of the permanent magnet in the dynamic microphone?",
            options: ["To attract iron particles for cleaning", "To create the stationary magnetic field", "To amplify the audio signal", "To secure the microphone to a stand"],
            correct: 1,
            explanation: "The permanent magnet establishes the stationary magnetic field required for electromagnetic induction to occur when the coil moves.",
            difficulty: "Medium"
        }
    ];

    function animate(time, speed, meshes) {
        // Simulate sound waves causing diaphragm and coil to vibrate
        const vibration = Math.sin(time * speed * 20) * 0.05 * Math.sin(time * speed * 5); // Complex waveform
        
        diaphragm.position.y = 1.1 + vibration;
        diaphragmGlow.position.y = 1.1 + vibration;
        
        coil.position.y = 1.3 + vibration;
        coilGlow.position.y = 1.3 + vibration;
        
        // Pulse glowing materials
        glowRed.emissiveIntensity = 1.0 + Math.abs(vibration) * 20;
        glowPurple.emissiveIntensity = 1.5 + Math.sin(time * speed * 10) * 0.5;
        glowBlue.emissiveIntensity = 1.0 + Math.sin(time * speed * 2) * 0.5;
        
        // Spin the magnetic field visual slightly
        core.rotation.y += 0.01 * speed;
        
        // Breathe effect on grille wireframe opacity
        grilleMaterial.opacity = 0.4 + Math.sin(time * speed * 3) * 0.1;
    }

    return { group, parts, description, quizQuestions, animate };
}

