import { plastic, aluminum, glass, copper, steel, darkSteel, rubber, chrome, tinted } from '../utils/materials.js';

export function createMachine(THREE) {
    const group = new THREE.Group();
    
    // Custom High-Tech Glowing Materials
    const neonBlue = new THREE.MeshPhysicalMaterial({
        color: 0x0088ff,
        emissive: 0x0088ff,
        emissiveIntensity: 0.8,
        transparent: true,
        opacity: 0.7,
        roughness: 0.1,
        metalness: 0.5
    });

    const shockwaveMaterial = new THREE.MeshPhysicalMaterial({
        color: 0x00ffff,
        emissive: 0x00aaff,
        emissiveIntensity: 0.5,
        transparent: true,
        opacity: 0.3,
        side: THREE.DoubleSide,
        depthWrite: false,
        blending: THREE.AdditiveBlending
    });

    const heatShieldMaterial = new THREE.MeshPhysicalMaterial({
        color: 0xff4400,
        emissive: 0xff2200,
        emissiveIntensity: 0.6,
        roughness: 0.3,
        metalness: 0.8
    });

    const sensorMaterial = new THREE.MeshStandardMaterial({
        color: 0x222222,
        roughness: 0.8,
        metalness: 0.1
    });

    const parts = [];
    
    // 1. Nose Cone
    const coneGeometry = new THREE.ConeGeometry(0.5, 3, 32);
    const noseCone = new THREE.Mesh(coneGeometry, chrome);
    noseCone.rotation.x = Math.PI / 2;
    noseCone.position.set(0, 0, 1.5);
    noseCone.castShadow = true;
    noseCone.receiveShadow = true;
    group.add(noseCone);
    parts.push({
        name: 'Supersonic Nose Cone',
        description: 'Aerodynamic shape designed to pierce through air at supersonic speeds, minimizing drag.',
        material: 'Chrome',
        function: 'Reduces wave drag and handles initial aerodynamic heating.',
        assemblyOrder: 1,
        connections: ['Fuselage', 'Shockwave Generator'],
        failureEffect: 'Severe increase in drag and structural heating.',
        cascadeFailures: ['Thermal Overload', 'Structural Disintegration'],
        originalPosition: { x: 0, y: 0, z: 1.5 },
        explodedPosition: { x: 0, y: 0, z: 3.5 },
        mesh: noseCone
    });

    // 2. Heat Shield
    const shieldGeometry = new THREE.ConeGeometry(0.51, 0.5, 32);
    const heatShield = new THREE.Mesh(shieldGeometry, heatShieldMaterial);
    heatShield.rotation.x = Math.PI / 2;
    heatShield.position.set(0, 0, 2.8);
    group.add(heatShield);
    parts.push({
        name: 'Ablative Heat Shield',
        description: 'Tip of the nose cone subjected to extreme thermal stress.',
        material: 'Ablative Ceramic / Glowing',
        function: 'Dissipates heat generated by aerodynamic friction at Mach 2+.',
        assemblyOrder: 2,
        connections: ['Nose Cone'],
        failureEffect: 'Melting of the nose cone tip.',
        cascadeFailures: ['Aerodynamic Instability', 'Total Loss'],
        originalPosition: { x: 0, y: 0, z: 2.8 },
        explodedPosition: { x: 0, y: 0, z: 4.5 },
        mesh: heatShield
    });

    // 3. Shockwave Visualization
    const shockwaveGeometry = new THREE.ConeGeometry(1.5, 3.5, 64, 1, true);
    const shockwave = new THREE.Mesh(shockwaveGeometry, shockwaveMaterial);
    shockwave.rotation.x = Math.PI / 2;
    shockwave.position.set(0, 0, 1.3);
    group.add(shockwave);
    parts.push({
        name: 'Oblique Shockwave',
        description: 'Visual representation of the shockwave generated at supersonic speeds.',
        material: 'Energy / Neon Blue',
        function: 'Abrupt change in pressure, temperature, and density of air.',
        assemblyOrder: 3,
        connections: ['Nose Cone (Aerodynamic interaction)'],
        failureEffect: 'N/A (Phenomenon)',
        cascadeFailures: [],
        originalPosition: { x: 0, y: 0, z: 1.3 },
        explodedPosition: { x: 0, y: 1.5, z: 1.3 },
        mesh: shockwave
    });

    // 4. Instrumentation Ring (Pitot Tubes / Sensors)
    const ringGeometry = new THREE.TorusGeometry(0.5, 0.05, 16, 32);
    const sensorRing = new THREE.Mesh(ringGeometry, neonBlue);
    sensorRing.position.set(0, 0, 0.5);
    group.add(sensorRing);
    parts.push({
        name: 'Telemetry Sensor Ring',
        description: 'Array of high-fidelity pressure and temperature sensors.',
        material: 'Neon Blue / Steel',
        function: 'Measures dynamic pressure and structural stress in real-time.',
        assemblyOrder: 4,
        connections: ['Nose Cone', 'Fuselage'],
        failureEffect: 'Loss of critical aerodynamic data.',
        cascadeFailures: ['Flight Computer Error', 'Incorrect Trim'],
        originalPosition: { x: 0, y: 0, z: 0.5 },
        explodedPosition: { x: 0, y: -1.5, z: 0.5 },
        mesh: sensorRing
    });

    // 5. Base/Fuselage connector
    const baseGeometry = new THREE.CylinderGeometry(0.5, 0.5, 1, 32);
    const baseCylinder = new THREE.Mesh(baseGeometry, darkSteel);
    baseCylinder.rotation.x = Math.PI / 2;
    baseCylinder.position.set(0, 0, -0.5);
    group.add(baseCylinder);
    parts.push({
        name: 'Forward Fuselage Segment',
        description: 'Connects the supersonic cone to the main body.',
        material: 'Dark Steel',
        function: 'Structural integration and payload housing.',
        assemblyOrder: 5,
        connections: ['Nose Cone', 'Sensor Ring'],
        failureEffect: 'Structural decoupling.',
        cascadeFailures: ['Catastrophic Disassembly'],
        originalPosition: { x: 0, y: 0, z: -0.5 },
        explodedPosition: { x: 0, y: 0, z: -2.5 },
        mesh: baseCylinder
    });


    // Description
    const description = "The Aerodynamics Supersonic Cone is a highly engineered structure designed to minimize wave drag during supersonic flight. As the cone exceeds the speed of sound (Mach 1), it compresses the air ahead of it, generating an oblique shockwave. The sharp tip is specifically modeled to manage extreme aerodynamic heating and pressure gradients. Interactive features allow visualizing the shockwave dynamics and internal sensor rings.";

    // Quiz Questions
    const quizQuestions = [
        {
            question: "Why is a sharp cone typically preferred over a blunt nose for supersonic flight?",
            options: [
                "It looks more aesthetically pleasing.",
                "It reduces skin friction drag.",
                "It generates an oblique shockwave which produces less wave drag than a normal shockwave.",
                "It increases lift significantly."
            ],
            correct: 2,
            explanation: "At supersonic speeds, a sharp cone generates an attached oblique shockwave, which results in much less wave drag compared to the detached normal shockwave formed by a blunt body.",
            difficulty: "Medium"
        },
        {
            question: "What happens to the air pressure and temperature immediately after passing through the shockwave?",
            options: [
                "Pressure and temperature both decrease.",
                "Pressure and temperature both increase suddenly.",
                "Pressure increases, but temperature decreases.",
                "They remain completely unchanged."
            ],
            correct: 1,
            explanation: "A shockwave represents a nearly discontinuous boundary where supersonic flow is abruptly decelerated, causing a sudden and dramatic increase in pressure, temperature, and density.",
            difficulty: "Hard"
        },
        {
            question: "What is the primary purpose of the ablative heat shield at the tip?",
            options: [
                "To reflect radar waves.",
                "To generate thrust.",
                "To dissipate the extreme aerodynamic heating caused by air friction and compression.",
                "To store aerodynamic data."
            ],
            correct: 2,
            explanation: "At high supersonic or hypersonic speeds, aerodynamic heating at the stagnation point (the tip) is extreme. An ablative shield absorbs this heat by melting or vaporizing, protecting the underlying structure.",
            difficulty: "Medium"
        }
    ];

    function animate(time, speed, meshes) {
        // Animate the shockwave to simulate supersonic airflow
        if (meshes['Oblique Shockwave']) {
            const wave = meshes['Oblique Shockwave'];
            // Pulsate the opacity and slightly scale to simulate flow dynamics
            wave.material.opacity = 0.2 + Math.sin(time * 5 * speed) * 0.1;
            const scaleOffset = Math.sin(time * 10 * speed) * 0.02;
            wave.scale.set(1 + scaleOffset, 1, 1 + scaleOffset);
            
            // Adjust the angle based on speed
            const coneAngleOffset = Math.max(0, speed - 1) * 0.1;
            wave.scale.x = 1 - coneAngleOffset;
            wave.scale.z = 1 - coneAngleOffset;
        }

        // Rotate the sensor ring
        if (meshes['Telemetry Sensor Ring']) {
            meshes['Telemetry Sensor Ring'].rotation.z = time * speed;
        }
        
        // Heat shield glowing effect based on speed
        if (meshes['Ablative Heat Shield']) {
            meshes['Ablative Heat Shield'].material.emissiveIntensity = 0.5 + (speed * 0.5);
        }
    }

    return { group, parts, description, quizQuestions, animate };
}

