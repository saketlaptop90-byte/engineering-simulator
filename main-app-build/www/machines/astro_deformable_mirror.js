import { plastic, aluminum, glass, copper, steel, darkSteel, rubber, chrome, tinted } from '../utils/materials.js';

export function createMachine(THREE) {
    const group = new THREE.Group();
    const parts = [];
    const meshes = {};

    // Custom materials
    const glowingBlue = new THREE.MeshStandardMaterial({
        color: 0x0088ff,
        emissive: 0x0088ff,
        emissiveIntensity: 0.8,
        metalness: 0.2,
        roughness: 0.2,
        transparent: true,
        opacity: 0.9
    });

    const mirrorSurfaceMaterial = new THREE.MeshPhysicalMaterial({
        color: 0xffffff,
        metalness: 1.0,
        roughness: 0.05,
        clearcoat: 1.0,
        clearcoatRoughness: 0.1,
        reflectivity: 1.0
    });

    const actuatorMaterial = new THREE.MeshStandardMaterial({
        color: 0xffaa00,
        emissive: 0xaa5500,
        emissiveIntensity: 0.2,
        metalness: 0.6,
        roughness: 0.4
    });

    const wireMaterial = new THREE.MeshStandardMaterial({
        color: 0x222222,
        metalness: 0.1,
        roughness: 0.9
    });

    // 1. Base Plate
    const basePlateGeom = new THREE.CylinderGeometry(5, 5, 1, 64);
    const basePlate = new THREE.Mesh(basePlateGeom, darkSteel);
    basePlate.position.set(0, -1, 0);
    group.add(basePlate);
    parts.push({
        name: "Base Plate",
        description: "Heavy, stable foundation isolating the mirror assembly from structural vibrations.",
        material: "Dark Steel",
        function: "Provides a rigid reference plane for all actuators.",
        assemblyOrder: 1,
        connections: ["Actuators", "Cooling System"],
        failureEffect: "Whole mirror becomes misaligned, severely distorting the image.",
        cascadeFailures: ["Actuator Binding"],
        originalPosition: { x: 0, y: -1, z: 0 },
        explodedPosition: { x: 0, y: -5, z: 0 }
    });
    meshes.basePlate = basePlate;

    // 2. Cooling System / Heat Sink
    const heatSinkGeom = new THREE.TorusGeometry(5, 0.5, 16, 64);
    const heatSink = new THREE.Mesh(heatSinkGeom, copper);
    heatSink.position.set(0, -0.5, 0);
    heatSink.rotation.x = Math.PI / 2;
    group.add(heatSink);
    parts.push({
        name: "Heat Sink Ring",
        description: "Copper cooling ring to dissipate heat generated by the hundreds of high-frequency actuators.",
        material: "Copper",
        function: "Maintains thermal stability of the mirror assembly.",
        assemblyOrder: 2,
        connections: ["Base Plate"],
        failureEffect: "Thermal expansion warps the base, causing uncorrectable low-order aberrations.",
        cascadeFailures: ["Mirror Surface Distortion"],
        originalPosition: { x: 0, y: -0.5, z: 0 },
        explodedPosition: { x: 0, y: -3, z: 0 }
    });
    meshes.heatSink = heatSink;

    // 3. Actuator Array (Piezoelectric)
    const actuatorGroup = new THREE.Group();
    const actuators = [];
    const numRings = 6;
    const spacing = 0.6;
    
    // Center actuator
    const actGeom = new THREE.CylinderGeometry(0.15, 0.15, 1.5, 16);
    for (let r = 0; r <= numRings; r++) {
        let count = r === 0 ? 1 : r * 6;
        for (let i = 0; i < count; i++) {
            let angle = (i / count) * Math.PI * 2;
            let radius = r * spacing;
            
            let actuator = new THREE.Mesh(actGeom, actuatorMaterial);
            let x = Math.cos(angle) * radius;
            let z = Math.sin(angle) * radius;
            
            actuator.position.set(x, 0, z);
            
            // Add a little glowing cap
            let capGeom = new THREE.SphereGeometry(0.16, 8, 8);
            let cap = new THREE.Mesh(capGeom, glowingBlue);
            cap.position.y = 0.75;
            actuator.add(cap);
            
            actuatorGroup.add(actuator);
            actuators.push({ mesh: actuator, baseLength: 1.5, startX: x, startZ: z, phase: Math.random() * Math.PI * 2 });
        }
    }
    group.add(actuatorGroup);
    parts.push({
        name: "Piezoelectric Actuator Array",
        description: "Hundreds of independent piezo stacks capable of sub-nanometer movements at thousands of Hertz.",
        material: "PZT Ceramic & Chrome",
        function: "Pushes and pulls the flexible face sheet to correct incoming wavefront errors in real-time.",
        assemblyOrder: 3,
        connections: ["Base Plate", "Flexible Face Sheet", "Control Wiring"],
        failureEffect: "Localized dead zones on the mirror, resulting in specific optical aberrations (like astigmatism or coma) remaining uncorrected.",
        cascadeFailures: ["Face Sheet Stress Fractures"],
        originalPosition: { x: 0, y: 0, z: 0 },
        explodedPosition: { x: 0, y: 0, z: 0 }
    });
    meshes.actuatorGroup = actuatorGroup;
    meshes.actuators = actuators;

    // 4. Control Wiring Array
    const wireGroup = new THREE.Group();
    const wireGeom = new THREE.CylinderGeometry(0.02, 0.02, 2, 8);
    for (let i = 0; i < actuators.length; i++) {
        let wire = new THREE.Mesh(wireGeom, wireMaterial);
        let a = actuators[i];
        wire.position.set(a.startX, -1.0, a.startZ);
        wireGroup.add(wire);
    }
    group.add(wireGroup);
    parts.push({
        name: "Control Wiring Bundle",
        description: "High-density wiring routing high-voltage signals from the real-time computer to each actuator.",
        material: "Rubber & Copper",
        function: "Delivers the control voltages calculated by the wavefront sensor processor.",
        assemblyOrder: 4,
        connections: ["Actuator Array"],
        failureEffect: "Loss of signal to a quadrant or ring of actuators, causing catastrophic degradation in seeing quality.",
        cascadeFailures: ["Actuator Arcing"],
        originalPosition: { x: 0, y: -1.0, z: 0 },
        explodedPosition: { x: 0, y: -8, z: 0 }
    });
    meshes.wireGroup = wireGroup;

    // 5. Flexible Face Sheet (The Mirror)
    // We will use a plane geometry with high segment count so we can deform its vertices
    const faceSheetGeom = new THREE.CylinderGeometry(4.2, 4.2, 0.05, 128, 16);
    const faceSheet = new THREE.Mesh(faceSheetGeom, mirrorSurfaceMaterial);
    faceSheet.position.set(0, 0.8, 0);
    group.add(faceSheet);
    parts.push({
        name: "Continuous Face Sheet",
        description: "An ultra-thin, highly reflective flexible membrane (often glass or silicon) bonded to the actuators.",
        material: "Coated Glass",
        function: "Reflects the starlight while dynamically bending to cancel out atmospheric turbulence.",
        assemblyOrder: 5,
        connections: ["Actuator Array"],
        failureEffect: "Cracking or delamination, instantly destroying the mirror's optical properties.",
        cascadeFailures: ["Complete System Failure"],
        originalPosition: { x: 0, y: 0.8, z: 0 },
        explodedPosition: { x: 0, y: 4, z: 0 }
    });
    meshes.faceSheet = faceSheet;
    
    // Store original vertex positions for deformation
    meshes.faceSheetOriginalPos = [];
    const posAttribute = faceSheet.geometry.attributes.position;
    for (let i = 0; i < posAttribute.count; i++) {
        meshes.faceSheetOriginalPos.push(new THREE.Vector3(
            posAttribute.getX(i),
            posAttribute.getY(i),
            posAttribute.getZ(i)
        ));
    }

    // 6. Protective Housing
    const housingGeom = new THREE.TorusGeometry(4.3, 0.4, 32, 128);
    const housing = new THREE.Mesh(housingGeom, aluminum);
    housing.rotation.x = Math.PI / 2;
    housing.position.set(0, 0.8, 0);
    group.add(housing);
    parts.push({
        name: "Retaining Bezel",
        description: "Outer ring that secures the edges of the face sheet and houses calibration sensors.",
        material: "Aluminum",
        function: "Provides a fixed boundary condition for the membrane and protects the fragile edges.",
        assemblyOrder: 6,
        connections: ["Face Sheet", "Base Plate"],
        failureEffect: "Edge curling of the mirror, causing severe aberrations at the pupil margins.",
        cascadeFailures: [],
        originalPosition: { x: 0, y: 0.8, z: 0 },
        explodedPosition: { x: 0, y: 6, z: 0 }
    });
    meshes.housing = housing;

    // Educational Quiz
    const quizQuestions = [
        {
            question: "What is the primary purpose of a deformable mirror in an astronomical telescope?",
            options: [
                "To focus light onto the secondary mirror",
                "To correct wavefront distortions caused by atmospheric turbulence in real-time",
                "To filter out specific wavelengths of light",
                "To cool the camera sensor"
            ],
            correct: 1,
            explanation: "Deformable mirrors are the core of Adaptive Optics systems. They rapidly change shape to cancel out the blurring effect of Earth's atmosphere, allowing ground-based telescopes to achieve space-telescope-like resolution.",
            difficulty: "Medium"
        },
        {
            question: "Why does the deformable mirror require hundreds or thousands of actuators?",
            options: [
                "To make the mirror heavier and more stable",
                "Because each actuator only works for a few minutes",
                "To provide high-spatial-resolution correction across the incoming wavefront",
                "To generate enough heat to prevent condensation"
            ],
            correct: 2,
            explanation: "Atmospheric turbulence creates complex, high-order distortions. A high density of actuators allows the mirror to bend in highly complex shapes to accurately reverse these microscopic, localized distortions.",
            difficulty: "Hard"
        },
        {
            question: "Which material is commonly used for the actuators due to its ability to change size when a voltage is applied?",
            options: [
                "Piezoelectric ceramics (like PZT)",
                "Carbon Fiber",
                "Neodymium magnets",
                "Teflon"
            ],
            correct: 0,
            explanation: "Piezoelectric materials physically expand or contract proportionally to the applied electrical voltage, providing the extremely fast, sub-nanometer precision movements required for adaptive optics.",
            difficulty: "Medium"
        }
    ];

    // Animation function
    function animate(time, speed, meshesObj) {
        const t = time * speed * 2.0; // Fast movement to simulate high Hz correction
        
        let actScaleArray = [];

        // 1. Animate Actuators
        meshesObj.actuators.forEach((act, idx) => {
            // Combine low frequency (tilt/tip) and high frequency (high order) noise
            let dist = Math.sqrt(act.startX*act.startX + act.startZ*act.startZ);
            
            let lowOrder = Math.sin(t + act.startX) * Math.cos(t * 0.8 + act.startZ);
            let highOrder = Math.sin(t * 3 + act.phase) * 0.5;
            let ripple = Math.sin(dist * 2 - t * 5) * 0.3;
            
            let stroke = (lowOrder + highOrder + ripple) * 0.05; // 5% deformation
            
            act.mesh.scale.y = 1.0 + stroke;
            actScaleArray.push(stroke);
            
            // Adjust cap glowing based on extension
            act.mesh.children[0].material.emissiveIntensity = 0.5 + stroke * 10;
        });

        // 2. Animate Face Sheet based on actuators
        const posAttribute = meshesObj.faceSheet.geometry.attributes.position;
        const origPos = meshesObj.faceSheetOriginalPos;
        
        for (let i = 0; i < posAttribute.count; i++) {
            let orig = origPos[i];
            
            // If it's a top surface vertex (y > 0 relative to cylinder center)
            if (orig.y > 0) {
                let dist = Math.sqrt(orig.x*orig.x + orig.z*orig.z);
                
                // Keep edges relatively fixed due to retaining bezel (radius ~4.2)
                let edgeAttenuation = Math.max(0, 1.0 - (dist / 4.2));
                edgeAttenuation = Math.pow(edgeAttenuation, 0.5); // Smoother falloff
                
                let lowOrder = Math.sin(t + orig.x) * Math.cos(t * 0.8 + orig.z);
                let highOrder = Math.sin(t * 3 + (orig.x * orig.z)) * 0.5;
                let ripple = Math.sin(dist * 2 - t * 5) * 0.3;
                
                // Amplitude of deformation
                let dy = (lowOrder + highOrder + ripple) * 0.15 * edgeAttenuation;
                
                posAttribute.setY(i, orig.y + dy);
            }
        }
        
        // Notify THREE.js that vertices have changed
        posAttribute.needsUpdate = true;
        meshesObj.faceSheet.geometry.computeVertexNormals();
        
        // Subtle heat sink rotation or glow
        meshesObj.heatSink.rotation.z = t * 0.1;
    }

    return { group, parts, description: "Adaptive Optics Deformable Mirror", quizQuestions, animate, meshes };
}

